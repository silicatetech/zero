#[cfg(feature = "streaming-mode")]
#[allow(static_mut_refs)]
unsafe fn begin_dream_session() -> (u64, [u32; STREAMING_MEMORY_TOKENS], usize) {
    DREAM_SESSION_ID = DREAM_SESSION_ID.wrapping_add(1);

    let mut memory = [0u32; STREAMING_MEMORY_TOKENS];
    let len = DREAM_MEMORY_LEN;
    let mut i = 0;
    while i < len {
        memory[i] = DREAM_MEMORY_TOKENS[i];
        i += 1;
    }

    (DREAM_SESSION_ID, memory, len)
}

#[cfg(feature = "streaming-mode")]
#[allow(static_mut_refs)]
unsafe fn commit_dream_memory(sequence: &[u32], generated_start: usize, sequence_len: usize) {
    let mut eligible = 0usize;
    let mut i = generated_start;
    while i < sequence_len {
        let token = sequence[i];
        if token != EOS_TOKEN_ENDOFTEXT && token != EOS_TOKEN_IM_END {
            eligible += 1;
        }
        i += 1;
    }

    let skip = eligible.saturating_sub(STREAMING_MEMORY_TOKENS);
    let mut seen = 0usize;
    let mut written = 0usize;
    i = generated_start;
    while i < sequence_len && written < STREAMING_MEMORY_TOKENS {
        let token = sequence[i];
        if token != EOS_TOKEN_ENDOFTEXT && token != EOS_TOKEN_IM_END {
            if seen >= skip {
                DREAM_MEMORY_TOKENS[written] = token;
                written += 1;
            }
            seen += 1;
        }
        i += 1;
    }

    while written < STREAMING_MEMORY_TOKENS {
        DREAM_MEMORY_TOKENS[written] = 0;
        written += 1;
    }
    DREAM_MEMORY_LEN = eligible.min(STREAMING_MEMORY_TOKENS);
}

