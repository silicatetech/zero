/// Utility for formatting tensor names into a fixed byte buffer (no_std compatible).
struct NameWriter<'a> {
    buf: &'a mut [u8],
    pos: usize,
}

impl<'a> core::fmt::Write for NameWriter<'a> {
    fn write_str(&mut self, s: &str) -> core::fmt::Result {
        let bytes = s.as_bytes();
        let end = self.pos + bytes.len();
        if end <= self.buf.len() {
            self.buf[self.pos..end].copy_from_slice(bytes);
            self.pos = end;
            Ok(())
        } else {
            Err(core::fmt::Error)
        }
    }
}

/// Get tensor weight bytes with quant-aware sizing.
///
/// Per-quant block geometry is sourced from the sacred parser's
/// `dequant` module so this stays in lockstep with the dequantisers
/// the inference path eventually feeds them into.
///
/// Supported quants (byte-sizing only — dequant lives in the forward
/// pass; tensors whose dequant kernel is missing will still surface
/// here as a correctly-sized slice and fail loudly downstream):
///   * `Q4K`  — 256 / 144 B.
///   * `Q5K`  — 256 / 176 B.
///   * `Q6K`  — 256 / 210 B.
///   * `Q3K`  — 256 / 110 B.
///   * `Q2K`  — 256 / 84 B.
///   * `Q8K`  — 256 / 292 B.
///   * `Q4_0` — 32 / 18 B. Bulk Kimi K2.6 layer weights.
///   * `Q4_1` — 32 / 20 B.
///   * `Q5_0` — 32 / 22 B.
///   * `Q5_1` — 32 / 24 B.
///   * `Q8_0` — 32 / 34 B. Embeddings / LM head in mixed-quant K2.6 builds.
///   * `Q8_1` — 32 / 36 B.
///   * `IQ4NL` — 32 / 18 B.
///   * `F32`  — 4 bytes per element.
///   * `F16`/`BF16` — 2 bytes per element.
///
/// Unsupported types print a `[QUANT]` diagnostic and return `None`
/// so silent load failures (the original Kimi K2.6 MoE symptom) are
/// observable on the serial console.
/// Boot-time weight-integrity scan. Quantised block scales (the leading
/// fp16 of each Q4_0/Q8_0 block) are always small (|d| < ~1); a scale > 64
/// proves the bytes are garbage — almost always a memory overlap stomping
/// the model payload in RAM (the aarch64 KV-arena/.smodel overlap was
/// exactly this). Called for tensors spread across the payload (low-offset
/// attention, high-offset LM head) so a memory overlap is caught wherever
/// it lands — and distinguished from a downstream compute/dispatch bug.
/// Samples rows across the tensor plus the last rows; O(sample), logs the
/// first bad row with the tensor name.
fn check_weight_integrity(
    name: &str,
    output_w: &[u8],
    quant: zero_gguf_parser::GgmlType,
    in_dim: usize,
) {
    use zero_gguf_parser::GgmlType;
    let (block_size, block_bytes) = match quant {
        GgmlType::Q8_0 => (32usize, 34usize),
        GgmlType::Q4_0 => (32usize, 18usize),
        // Other quants encode scales sub-block, so the simple fp16-head
        // check does not apply. The native .smodel-v2 LM head is Q8_0.
        _ => return,
    };
    if in_dim % block_size != 0 {
        return;
    }
    let blocks_per_row = in_dim / block_size;
    let bytes_per_row = blocks_per_row * block_bytes;
    if bytes_per_row == 0 {
        return;
    }
    let n_rows = output_w.len() / bytes_per_row;
    if n_rows == 0 {
        return;
    }
    // `.smodel`-v2 stores matmul matrices row-interleaved (Q4_0X4 /
    // Q8_0X4): within each group of `group` output rows the per-block fp16
    // scales are packed at the FRONT of the group-block, followed by the
    // interleaved quant payload. The frozen dispatch `GgmlType` cannot
    // carry that layout flag, so the interleave factor lives in the same
    // registry the matmul kernels consult (`weight_layout`). Reading
    // scales at the PLAIN row stride on an interleaved tensor lands in the
    // quant payload after block 0 and misreads nibble/int8 bytes as huge
    // fp16 scales — a FALSE "CORRUPT". The group-aware offset below reads
    // each scale at its true position and reduces exactly to the plain
    // row-major layout at group == 1, so genuine stomps are still caught.
    let group = crate::weight_layout::group_of(output_w.as_ptr() as usize).max(1) as usize;
    let group_block_bytes = group * block_bytes;
    let group_stride = blocks_per_row * group_block_bytes;
    // fp16 -> f32 magnitude (abs); only need to detect absurdly large.
    let fp16_abs = |bits: u16| -> f32 {
        let exp = ((bits >> 10) & 0x1f) as u32;
        let mant = (bits & 0x3ff) as u32;
        if exp == 0 {
            (mant as f32) * (1.0 / 1024.0) * (1.0 / 16384.0)
        } else if exp == 31 {
            f32::INFINITY
        } else {
            f32::from_bits(((exp - 15 + 127) << 23) | (mant << 13))
        }
    };
    let mut checked = 0usize;
    let mut scan_row = |row: usize| -> bool {
        let gi = row / group;
        let lane = row % group;
        let group_base = gi * group_stride;
        let mut b = 0usize;
        while b < blocks_per_row {
            // fp16 scale for (row, block b): front-packed at `lane*2`
            // within this group-block. At group == 1 this is exactly
            // `row * bytes_per_row + b * block_bytes`.
            let bo = group_base + b * group_block_bytes + lane * 2;
            if bo + 1 >= output_w.len() {
                break;
            }
            let sc = fp16_abs(u16::from_le_bytes([output_w[bo], output_w[bo + 1]]));
            if !(sc < 64.0) {
                let _ = writeln!(
                    arch::serial::Serial,
                    "[MP2.5][INTEGRITY] CORRUPT {}: row {} block {} scale={:.3e} (>64 ⇒ model payload stomped in RAM — arena/model overlap)",
                    name, row, b, sc
                );
                return true;
            }
            b += 1;
        }
        false
    };
    // Sparse sweep across the whole tensor.
    let step = (n_rows / 512).max(1);
    let mut row = 0usize;
    while row < n_rows {
        if scan_row(row) {
            return;
        }
        checked += 1;
        row += step;
    }
    // Dense sweep over the last 2048 rows (most exposed to a tail overlap).
    let tail_start = n_rows.saturating_sub(2048);
    let mut row = tail_start;
    while row < n_rows {
        if scan_row(row) {
            return;
        }
        checked += 1;
        row += 1;
    }
    let _ = writeln!(
        arch::serial::Serial,
        "[MP2.5][INTEGRITY] {} OK ({} rows sampled, {} total, quant {:?}, interleave {})",
        name, checked, n_rows, quant, group
    );
}

fn get_weight_bytes<'a>(
    model_bytes: &'a [u8],
    tensor_index: &zero_gguf_parser::TensorIndex,
    name: &str,
) -> Option<&'a [u8]> {
    use zero_gguf_parser::dequant::{
        IQ4NL_BLOCK_BYTES, IQ4NL_BLOCK_SIZE, Q2K_BLOCK_BYTES, Q2K_BLOCK_SIZE, Q3K_BLOCK_BYTES,
        Q3K_BLOCK_SIZE, Q4K_BLOCK_BYTES, Q4K_BLOCK_SIZE, Q4_0_BLOCK_BYTES, Q4_0_BLOCK_SIZE,
        Q4_1_BLOCK_BYTES, Q4_1_BLOCK_SIZE, Q5K_BLOCK_BYTES, Q5K_BLOCK_SIZE, Q5_0_BLOCK_BYTES,
        Q5_0_BLOCK_SIZE, Q5_1_BLOCK_BYTES, Q5_1_BLOCK_SIZE, Q6K_BLOCK_BYTES, Q6K_BLOCK_SIZE,
        Q8K_BLOCK_BYTES, Q8K_BLOCK_SIZE, Q8_0_BLOCK_BYTES, Q8_0_BLOCK_SIZE, Q8_1_BLOCK_BYTES,
        Q8_1_BLOCK_SIZE,
    };

    let t = tensor_index.get(name)?;
    let off = tensor_index.tensor_data_offset + t.offset as usize;
    let n_elements = t.element_count() as usize;
    let (bytes_per_block, block_size) = match t.tensor_type {
        zero_gguf_parser::GgmlType::Q4K => (Q4K_BLOCK_BYTES, Q4K_BLOCK_SIZE),
        zero_gguf_parser::GgmlType::Q6K => (Q6K_BLOCK_BYTES, Q6K_BLOCK_SIZE),
        zero_gguf_parser::GgmlType::Q4_0 => (Q4_0_BLOCK_BYTES, Q4_0_BLOCK_SIZE),
        zero_gguf_parser::GgmlType::Q8_0 => (Q8_0_BLOCK_BYTES, Q8_0_BLOCK_SIZE),
        zero_gguf_parser::GgmlType::Q5K => (Q5K_BLOCK_BYTES, Q5K_BLOCK_SIZE),
        zero_gguf_parser::GgmlType::Q3K => (Q3K_BLOCK_BYTES, Q3K_BLOCK_SIZE),
        zero_gguf_parser::GgmlType::Q2K => (Q2K_BLOCK_BYTES, Q2K_BLOCK_SIZE),
        zero_gguf_parser::GgmlType::Q8K => (Q8K_BLOCK_BYTES, Q8K_BLOCK_SIZE),
        zero_gguf_parser::GgmlType::Q5_0 => (Q5_0_BLOCK_BYTES, Q5_0_BLOCK_SIZE),
        zero_gguf_parser::GgmlType::Q5_1 => (Q5_1_BLOCK_BYTES, Q5_1_BLOCK_SIZE),
        zero_gguf_parser::GgmlType::Q4_1 => (Q4_1_BLOCK_BYTES, Q4_1_BLOCK_SIZE),
        zero_gguf_parser::GgmlType::Q8_1 => (Q8_1_BLOCK_BYTES, Q8_1_BLOCK_SIZE),
        zero_gguf_parser::GgmlType::IQ4NL => (IQ4NL_BLOCK_BYTES, IQ4NL_BLOCK_SIZE),
        zero_gguf_parser::GgmlType::F32 => {
            let end = off + n_elements * 4;
            if end > model_bytes.len() {
                let _ = writeln!(
                    arch::serial::Serial,
                    "[BOUNDS] F32 tensor '{}' end=0x{:x} exceeds model_bytes len=0x{:x}",
                    name,
                    end,
                    model_bytes.len()
                );
                return None;
            }
            return Some(&model_bytes[off..end]);
        }
        // F16 and BF16 are 2 bytes per element with no block structure.
        zero_gguf_parser::GgmlType::F16 | zero_gguf_parser::GgmlType::BF16 => {
            let end = off + n_elements * 2;
            if end > model_bytes.len() {
                let _ = writeln!(
                    arch::serial::Serial,
                    "[BOUNDS] 16-bit tensor '{}' end=0x{:x} exceeds model_bytes len=0x{:x}",
                    name,
                    end,
                    model_bytes.len()
                );
                return None;
            }
            return Some(&model_bytes[off..end]);
        }
        other => {
            let _ =
                writeln!(arch::serial::Serial,
                "[QUANT] tensor '{}' unsupported quant type {} — get_weight_bytes returning None",
                name, other as u32);
            return None;
        }
    };
    if block_size == 0 || n_elements % block_size != 0 {
        return None;
    }
    let n_blocks = n_elements / block_size;
    let end = off + n_blocks * bytes_per_block;
    if end > model_bytes.len() {
        let _ = writeln!(
            arch::serial::Serial,
            "[BOUNDS] tensor '{}' end=0x{:x} exceeds model_bytes len=0x{:x}",
            name,
            end,
            model_bytes.len()
        );
        return None;
    }
    Some(&model_bytes[off..end])
}

/// Get F32 weight slice from tensor index.
///
/// SAFETY: bounds-checked — returns None if the requested slice would
/// exceed model_bytes.
fn get_f32_weight<'a>(
    model_bytes: &'a [u8],
    tensor_index: &zero_gguf_parser::TensorIndex,
    name: &str,
    n_floats: usize,
) -> Option<&'a [f32]> {
    let t = tensor_index.get(name)?;
    let off = tensor_index.tensor_data_offset + t.offset as usize;
    let end = off + n_floats * 4;
    if end > model_bytes.len() {
        let _ = writeln!(
            arch::serial::Serial,
            "[BOUNDS] f32 tensor '{}' off=0x{:x} need={} bytes, model_bytes len=0x{:x}",
            name,
            off,
            n_floats * 4,
            model_bytes.len()
        );
        return None;
    }
    // Alignment check — f32 requires 4-byte alignment.
    let ptr = unsafe { model_bytes.as_ptr().add(off) };
    if (ptr as usize) % 4 != 0 {
        let _ = writeln!(
            arch::serial::Serial,
            "[BOUNDS] f32 tensor '{}' misaligned: ptr=0x{:x}",
            name,
            ptr as usize
        );
        return None;
    }
    Some(unsafe { core::slice::from_raw_parts(ptr as *const f32, n_floats) })
}

/// Format a per-layer tensor name into a buffer.
fn fmt_name(buf: &mut [u8], layer: usize, suffix: &str) -> usize {
    let mut w = NameWriter { buf, pos: 0 };
    let _ = write!(w, "blk.{}.{}", layer, suffix);
    w.pos
}

/// Look up quantized weight bytes for a specific layer tensor.
fn lookup_bytes<'a>(
    model_bytes: &'a [u8],
    tensor_index: &zero_gguf_parser::TensorIndex,
    layer: usize,
    suffix: &str,
) -> Option<&'a [u8]> {
    let mut nb = [0u8; 40];
    let len = fmt_name(&mut nb, layer, suffix);
    let name = core::str::from_utf8(&nb[..len]).ok()?;
    get_weight_bytes(model_bytes, tensor_index, name)
}

/// Look up F32 weight slice for a specific layer tensor.
fn lookup_f32<'a>(
    model_bytes: &'a [u8],
    tensor_index: &zero_gguf_parser::TensorIndex,
    layer: usize,
    suffix: &str,
    n: usize,
) -> Option<&'a [f32]> {
    let mut nb = [0u8; 40];
    let len = fmt_name(&mut nb, layer, suffix);
    let name = core::str::from_utf8(&nb[..len]).ok()?;
    get_f32_weight(model_bytes, tensor_index, name, n)
}

/// Look up the `GgmlType` (per-tensor quantisation) of a specific layer
/// tensor without dereferencing its bytes. Used by the deepseek2 path
/// to thread per-tensor quant dispatch through the MoE / MLA forward
/// pass without baking Q4_K assumptions into the call sites.
fn lookup_tensor_type(
    tensor_index: &zero_gguf_parser::TensorIndex,
    layer: usize,
    suffix: &str,
) -> Option<zero_gguf_parser::GgmlType> {
    let mut nb = [0u8; 48];
    let len = fmt_name(&mut nb, layer, suffix);
    let name = core::str::from_utf8(&nb[..len]).ok()?;
    Some(tensor_index.get(name)?.tensor_type)
}

/// Run the full Boot-LLM forward-pass: dispatch table → layer loading →
/// forward_single_token → lm_head_argmax.
///
/// This is the canonical cross-platform inference function (Pillar 7).
/// Called identically from both x86_64 and aarch64 with their respective
/// model_bytes slice (x86_64: via DirectMemoryLoader, aarch64: via
/// BOOT_LLM_VIRT_BASE).
///
/// Returns the predicted token ID on success.
pub fn run_forward_pass(model_bytes: &[u8], tensor_index: &zero_gguf_parser::TensorIndex) {
    // ── Architecture dispatch (Kimi K2.6 / DeepSeek2 vs Qwen3) ──────
    if tensor_index.model_config.is_deepseek2() {
        let _ = writeln!(
            arch::serial::Serial,
            "[MoE] deepseek2 architecture detected — entering DeepSeek2 / Kimi K2.6 forward path"
        );
        run_forward_pass_deepseek2(model_bytes, tensor_index);
        return;
    }

    // ADR-037: native `.smodel` artifacts carry strict validation anchors
    // in the SilicatePack manifest. Raw GGUF keeps the legacy beta-anchor.
    let native_smodel_profile = matches!(model_bytes.get(0..4), Some(magic) if magic == b"SILM");
    let smodel_validation_anchor = if native_smodel_profile {
        match crate::model_loader::smodel_validation_anchor(model_bytes) {
            Ok(anchor) => anchor,
            Err(_) => {
                let _ = writeln!(
                    arch::serial::Serial,
                    "[MP3.0] .smodel validation anchor parse FAILED — HALTING"
                );
                loop {
                    core::hint::spin_loop();
                }
            }
        }
    } else {
        None
    };

    let cfg = &tensor_index.model_config;
    let embedding_dim = cfg.embedding_length as usize;
    let intermediate_dim = cfg.feed_forward_length as usize;
    let n_q_heads = cfg.head_count as usize;
    let n_kv_heads = cfg.head_count_kv as usize;
    let head_dim = embedding_dim / n_q_heads;
    let rms_eps = cfg.layer_norm_rms_epsilon;
    let n_layers = cfg.block_count as usize;
    let q_dim = n_q_heads * head_dim;
    let kv_dim = n_kv_heads * head_dim;

    #[cfg(all(target_arch = "x86_64", feature = "avx512-acceleration"))]
    {
        let _ = writeln!(
            arch::serial::Serial,
            "[MP3.0] x86_64 AVX-512/SMP dispatch visible: active_cores={} registered_cores={}",
            crate::smp::active_cores(),
            crate::smp::registered_cores(),
        );
    }

    // Sub-MP-D3/G2: max context budget for autoregressive generation.
    #[cfg(not(feature = "streaming-mode"))]
    let max_tokens: usize = PROMPT_TOKEN_COUNT + MAX_GENERATED_TOKENS + 3;
    #[cfg(feature = "streaming-mode")]
    let max_tokens: usize = STREAMING_SEQUENCE_CAPACITY;

    #[cfg(feature = "streaming-mode")]
    let (dream_session_id, dream_memory_snapshot, dream_memory_len) =
        unsafe { begin_dream_session() };

    #[cfg(feature = "streaming-mode")]
    {
        let mut kv_guard = memory::KV_CACHE_ARENA.lock();
        if let Some(ref mut kv_arena) = *kv_guard {
            kv_arena.reset();
        }
        drop(kv_guard);

        let mut act_guard = memory::ACTIVATION_ARENA.lock();
        if let Some(ref mut act_arena) = *act_guard {
            act_arena.reset();
        }
    }

    // Build per-layer dispatch table. Defaults preserve the legacy Qwen3
    // Q4_K/Q6_K byte-accuracy path; native `.smodel` artifacts override each
    // projection from the tensor directory so Q4_0/Q8_0 models do not enter the
    // wrong dequantizer.
    let mut fp_dispatch = zero_llm_inference::ForwardPassDispatch::default();
    if let Some(t) = tensor_index.get("token_embd.weight") {
        fp_dispatch.token_embd_quant = t.tensor_type;
    }
    for layer in 0..n_layers {
        fp_dispatch.attn_q_quant[layer] = lookup_tensor_type(tensor_index, layer, "attn_q.weight")
            .unwrap_or(zero_gguf_parser::GgmlType::Q4K);
        fp_dispatch.attn_k_quant[layer] = lookup_tensor_type(tensor_index, layer, "attn_k.weight")
            .unwrap_or(zero_gguf_parser::GgmlType::Q4K);
        fp_dispatch.attn_v_tensor_quant[layer] =
            lookup_tensor_type(tensor_index, layer, "attn_v.weight")
                .unwrap_or(zero_gguf_parser::GgmlType::Q4K);
        fp_dispatch.attn_o_quant[layer] =
            lookup_tensor_type(tensor_index, layer, "attn_output.weight")
                .unwrap_or(zero_gguf_parser::GgmlType::Q4K);
        fp_dispatch.ffn_gate_quant[layer] =
            lookup_tensor_type(tensor_index, layer, "ffn_gate.weight")
                .unwrap_or(zero_gguf_parser::GgmlType::Q4K);
        fp_dispatch.ffn_up_quant[layer] = lookup_tensor_type(tensor_index, layer, "ffn_up.weight")
            .unwrap_or(zero_gguf_parser::GgmlType::Q4K);
        fp_dispatch.ffn_down_tensor_quant[layer] =
            lookup_tensor_type(tensor_index, layer, "ffn_down.weight")
                .unwrap_or(zero_gguf_parser::GgmlType::Q4K);

        if matches!(
            fp_dispatch.ffn_down_tensor_quant[layer],
            zero_gguf_parser::GgmlType::Q6K
        ) {
            fp_dispatch.ffn_down_quant[layer] = zero_llm_inference::FfnDownQuant::Q6K;
        }
        if matches!(
            fp_dispatch.attn_v_tensor_quant[layer],
            zero_gguf_parser::GgmlType::Q6K
        ) {
            fp_dispatch.attn_v_quant[layer] = zero_llm_inference::FfnDownQuant::Q6K;
        }
    }

    // Build LayerWeights for all 28 layers
    let mut layers_ok = true;
    let dummy_f32: &[f32] = &[];
    let dummy_u8: &[u8] = &[];
    let mut layer_arr: [zero_llm_inference::LayerWeights; 28] =
        core::array::from_fn(|_| zero_llm_inference::LayerWeights {
            attn_norm: dummy_f32,
            attn_q: dummy_u8,
            attn_k: dummy_u8,
            attn_v: dummy_u8,
            attn_o: dummy_u8,
            attn_q_norm: dummy_f32,
            attn_k_norm: dummy_f32,
            ffn_norm: dummy_f32,
            ffn_gate: dummy_u8,
            ffn_up: dummy_u8,
            ffn_down: dummy_u8,
        });

    for layer in 0..n_layers {
        let an = lookup_f32(
            model_bytes,
            tensor_index,
            layer,
            "attn_norm.weight",
            embedding_dim,
        );
        let aq = lookup_bytes(model_bytes, tensor_index, layer, "attn_q.weight");
        let ak = lookup_bytes(model_bytes, tensor_index, layer, "attn_k.weight");
        let av = lookup_bytes(model_bytes, tensor_index, layer, "attn_v.weight");
        let ao = lookup_bytes(model_bytes, tensor_index, layer, "attn_output.weight");
        let aqn = lookup_f32(
            model_bytes,
            tensor_index,
            layer,
            "attn_q_norm.weight",
            head_dim,
        );
        let akn = lookup_f32(
            model_bytes,
            tensor_index,
            layer,
            "attn_k_norm.weight",
            head_dim,
        );
        let fn_ = lookup_f32(
            model_bytes,
            tensor_index,
            layer,
            "ffn_norm.weight",
            embedding_dim,
        );
        let fg = lookup_bytes(model_bytes, tensor_index, layer, "ffn_gate.weight");
        let fu = lookup_bytes(model_bytes, tensor_index, layer, "ffn_up.weight");
        let fd = lookup_bytes(model_bytes, tensor_index, layer, "ffn_down.weight");

        if let (
            Some(an),
            Some(aq),
            Some(ak),
            Some(av),
            Some(ao),
            Some(aqn),
            Some(akn),
            Some(fn_),
            Some(fg),
            Some(fu),
            Some(fd),
        ) = (an, aq, ak, av, ao, aqn, akn, fn_, fg, fu, fd)
        {
            layer_arr[layer] = zero_llm_inference::LayerWeights {
                attn_norm: an,
                attn_q: aq,
                attn_k: ak,
                attn_v: av,
                attn_o: ao,
                attn_q_norm: aqn,
                attn_k_norm: akn,
                ffn_norm: fn_,
                ffn_gate: fg,
                ffn_up: fu,
                ffn_down: fd,
            };
        } else {
            let _ = write!(arch::serial::Serial, "[MP2.5] Missing tensors for layer ");
            let _ = writeln!(arch::serial::Serial, "{}", layer);
            layers_ok = false;
            break;
        }
    }

    if !layers_ok {
        return;
    }
    let _ = writeln!(arch::serial::Serial, "[MP2.5] All 28 layers loaded");

    // MP2.6 — SMP dispatch self-test on real layer-0 weights, BEFORE
    // the first forward pass. The parallel dispatcher (epochs, tree
    // barrier, closed-form row ranges, fused/x4 kernels) only ever ran
    // on real multi-core silicon starting with the first Cherry boot;
    // a silent dispatch defect would surface as garbage logits or a
    // phase-1 abort. One bitwise parallel-vs-single-core comparison
    // converts that failure mode into a logged single-core degrade —
    // slow but bit-exact, box stays measurable.
    #[cfg(all(target_arch = "x86_64", feature = "avx512-acceleration"))]
    {
        if matches!(
            fp_dispatch.attn_q_quant[0],
            zero_gguf_parser::GgmlType::Q4_0
        ) && !layer_arr[0].attn_q.is_empty()
        {
            let mut act_guard = memory::ACTIVATION_ARENA.lock();
            if let Some(ref mut arena) = *act_guard {
                if let Ok(scratch) = arena.alloc_f32_slice(embedding_dim + 2 * q_dim) {
                    let ok = unsafe {
                        crate::inference_avx512::smp_dispatch_self_test(
                            layer_arr[0].attn_q,
                            embedding_dim,
                            q_dim,
                            scratch,
                        )
                    };
                    if ok {
                        let _ = writeln!(
                            arch::serial::Serial,
                            "[MP2.6] SMP dispatch self-test PASS (active_cores={})",
                            crate::smp::active_cores()
                        );
                    } else {
                        let prev = crate::smp::set_max_matmul_cores(1);
                        let _ = writeln!(
                            arch::serial::Serial,
                            "[MP2.6] SMP dispatch self-test FAILED — capping matmul to single-core (was {}); inference continues bit-exact but slow",
                            prev
                        );
                    }
                }
            }
        }
    }

    let token_embd = get_weight_bytes(model_bytes, tensor_index, "token_embd.weight");
    let output_norm_w = get_f32_weight(
        model_bytes,
        tensor_index,
        "output_norm.weight",
        embedding_dim,
    );
    let output_w = get_weight_bytes(model_bytes, tensor_index, "output.weight");

    if let (Some(token_embd), Some(output_norm_w), Some(output_w)) =
        (token_embd, output_norm_w, output_w)
    {
        let output_quant = tensor_index
            .get("output.weight")
            .map(|t| t.tensor_type)
            .unwrap_or(zero_gguf_parser::GgmlType::Q6K);

        // Model-integrity gate (2026-06-13): a quantised weight block scale
        // is always small (|d| < ~1); a scale > 64 means the bytes are
        // garbage — almost always because an arena/allocation overlapped the
        // model payload in RAM (the aarch64 KV-arena/.smodel overlap was
        // exactly this). Scan tensors spread across the payload — the LM
        // head (HIGH offset; aarch64 corrupted its upper half) AND attention
        // weights from the first/mid/last layers (LOW/MID offsets; the
        // x86/Cherry failure is an attention-NaN, so the corrupted region
        // there, if any, is at the low/mid end). This makes the next boot
        // say explicitly WHICH region (if any) is stomped — distinguishing a
        // memory overlap from a downstream compute/dispatch bug.
        check_weight_integrity("output.weight", output_w, output_quant, embedding_dim);
        {
            let last = n_layers.saturating_sub(1);
            check_weight_integrity(
                "blk.0.attn_q",
                layer_arr[0].attn_q,
                fp_dispatch.attn_q_quant[0],
                embedding_dim,
            );
            check_weight_integrity(
                "blk.mid.attn_q",
                layer_arr[n_layers / 2].attn_q,
                fp_dispatch.attn_q_quant[n_layers / 2],
                embedding_dim,
            );
            check_weight_integrity(
                "blk.last.attn_q",
                layer_arr[last].attn_q,
                fp_dispatch.attn_q_quant[last],
                embedding_dim,
            );
        }
        #[cfg(not(any(
            all(target_arch = "aarch64", feature = "neon-acceleration"),
            all(target_arch = "x86_64", feature = "avx512-acceleration"),
        )))]
        let output_quant_scalar = match output_quant_from_ggml(output_quant) {
            Some(q) => q,
            None => {
                let _ = writeln!(
                    arch::serial::Serial,
                    "[MP2.5] output.weight quant unsupported"
                );
                return;
            }
        };

        // Sub-MP-D3: KV storage migrated to KV_CACHE_ARENA (512 MiB per ADR-030).
        // Previously inline in ACTIVATION_ARENA (16 MiB, insufficient for max_tokens=48).
        // KV total: 2 × n_layers × max_tokens × kv_dim f32s.
        let kv_total_f32 = 2 * n_layers * max_tokens * kv_dim;
        let mut kv_guard = memory::KV_CACHE_ARENA.lock();
        let kv_storage = if let Some(ref mut kv_arena) = *kv_guard {
            match kv_arena.alloc_f32_slice(kv_total_f32) {
                Ok(s) => s,
                Err(_) => {
                    let _ = writeln!(arch::serial::Serial, "[MP2.5] KV_CACHE_ARENA alloc failed");
                    return;
                }
            }
        } else {
            let _ = writeln!(
                arch::serial::Serial,
                "[MP2.5] KV_CACHE_ARENA not initialized"
            );
            return;
        };
        drop(kv_guard);

        let mut kv_cache = zero_llm_inference::KvCache::new(
            kv_storage.as_mut_ptr(),
            max_tokens,
            n_layers,
            n_kv_heads,
            head_dim,
        );

        // Allocate scratch via ACTIVATION_ARENA (KV storage no longer here)
        let total_f32 = embedding_dim
            + embedding_dim
            + embedding_dim
            + embedding_dim
            + q_dim
            + kv_dim
            + kv_dim
            + q_dim
            + kv_dim
            // score_buf: one stripe per Q-head (S1 parallel attention;
            // sequential paths keep using stripe 0 as before).
            + n_q_heads * max_tokens
            + head_dim
            + q_dim
            + intermediate_dim
            + intermediate_dim
            + intermediate_dim
            + zero_llm_inference::VOCAB_SIZE_PADDED;

        let mut act_guard = memory::ACTIVATION_ARENA.lock();
        if let Some(ref mut arena) = *act_guard {
            if let Ok(buf) = arena.alloc_f32_slice(total_f32) {
                let (hidden, rest) = buf.split_at_mut(embedding_dim);
                let (norm_buf, rest) = rest.split_at_mut(embedding_dim);
                let (attn_out, rest) = rest.split_at_mut(embedding_dim);
                let (mlp_out, rest) = rest.split_at_mut(embedding_dim);
                let (q_buf, rest) = rest.split_at_mut(q_dim);
                let (k_buf, rest) = rest.split_at_mut(kv_dim);
                let (v_buf, rest) = rest.split_at_mut(kv_dim);
                let (q_out, rest) = rest.split_at_mut(q_dim);
                let (k_out, rest) = rest.split_at_mut(kv_dim);
                let (score_buf, rest) = rest.split_at_mut(n_q_heads * max_tokens);
                let (attn_head_buf, rest) = rest.split_at_mut(head_dim);
                let (attn_concat, rest) = rest.split_at_mut(q_dim);
                let (gate_buf, rest) = rest.split_at_mut(intermediate_dim);
                let (up_buf, rest) = rest.split_at_mut(intermediate_dim);
                let (hidden_mlp_buf, rest) = rest.split_at_mut(intermediate_dim);
                let logits_buf = &mut rest[..zero_llm_inference::VOCAB_SIZE_PADDED];

                let rope_ctx = zero_llm_inference::RopeContext::<64>::new(1_000_000.0);
                let mut lin_scratch = zero_llm_inference::LinearScratch::new();

                // ─────────────────────────────────────────────────────────────
                // Sub-MP-D3 Task B: Multi-token autoregressive generation.
                //
                // Design decisions:
                //   C1: Greedy deterministic (sampling deferred to D3.5)
                //   C2: Serial console output only
                //   C3: Option γ Hybrid prompt (full prompt + AI continuation)
                //   γ-anchor: β-anchor (intermediate verification after token 0)
                //
                // Per V3.1 Pillar 7: canonical shared function, no platform branching.
                // Per V3.1 Pillar 8: Rust temporary, Quarks migration Stage 12+.
                // Per-token Top-K = continuous Pillar-7-verification.
                // ─────────────────────────────────────────────────────────────

                // β-anchor verification consts at point-of-consumption
                #[cfg(not(feature = "streaming-mode"))]
                const BETA_ANCHOR_EXPECTED_NEXT_TOKEN: u32 = 25;
                #[cfg(not(feature = "streaming-mode"))]
                const BETA_ANCHOR_EXPECTED_LOGIT_BITS: u32 = 0x414a6497;

                let _ = writeln!(
                    arch::serial::Serial,
                    "[MP3.0] Sub-MP-D3 autoregressive generation starting"
                );
                let _ = write!(arch::serial::Serial, "[MP3.0] Prompt tokens: ");
                let _ = write!(arch::serial::Serial, "{}", PROMPT_TOKEN_COUNT);
                let _ = write!(arch::serial::Serial, " | Max generated: ");
                let _ = write!(arch::serial::Serial, "{}", MAX_GENERATED_TOKENS);
                let _ = write!(arch::serial::Serial, " | Total budget: ");
                let _ = writeln!(arch::serial::Serial, "{}", max_tokens);
                #[cfg(feature = "streaming-mode")]
                {
                    let _ = write!(arch::serial::Serial, "[G2] Dream session ");
                    let _ = write!(arch::serial::Serial, "{}", dream_session_id);
                    let _ = write!(arch::serial::Serial, " | rolling memory tokens: ");
                    let _ = writeln!(arch::serial::Serial, "{}", dream_memory_len);
                }

                // Stack-allocated sequence buffer (V3.1 Pillar 1: zero allocation)
                #[cfg(not(feature = "streaming-mode"))]
                let mut sequence: [u32; 64] = [0; 64];
                #[cfg(feature = "streaming-mode")]
                let mut sequence: [u32; STREAMING_SEQUENCE_CAPACITY] =
                    [0; STREAMING_SEQUENCE_CAPACITY];
                let mut sequence_len: usize = 0;
                #[cfg(not(feature = "streaming-mode"))]
                let prefill_token_count = PROMPT_TOKEN_COUNT;
                #[cfg(feature = "streaming-mode")]
                let prefill_token_count = PROMPT_TOKEN_COUNT + dream_memory_len;

                // ─── PHASE 1: Prompt prefill (β-anchor after token 0) ───
                let _ = write!(arch::serial::Serial, "[MP3.0] Phase 1: Prompt prefill (");
                let _ = write!(arch::serial::Serial, "{}", prefill_token_count);
                let _ = writeln!(arch::serial::Serial, " tokens)");

                let mut phase1_ok = true;
                let profile_prefill_wall_t0 = arch::read_cycles();
                let mut profile_prefill_forward_cycles: u64 = 0;
                let mut profile_prefill_lm_head_cycles: u64 = 0;
                let mut profile_prefill_logit_scan_cycles: u64 = 0;

                for i in 0..prefill_token_count {
                    #[cfg(not(feature = "streaming-mode"))]
                    let prompt_token = PROMPT_TOKENS[i];
                    #[cfg(feature = "streaming-mode")]
                    let prompt_token = if i < PROMPT_TOKEN_COUNT {
                        PROMPT_TOKENS[i]
                    } else {
                        dream_memory_snapshot[i - PROMPT_TOKEN_COUNT]
                    };
                    sequence[sequence_len] = prompt_token;
                    let position = sequence_len;
                    sequence_len += 1;

                    if stage11_verbose_logging() {
                        let _ = write!(arch::serial::Serial, "[MP3.0] Prompt token ");
                        let _ = write!(arch::serial::Serial, "{}", i);
                        let _ = write!(arch::serial::Serial, "/");
                        let _ = write!(arch::serial::Serial, "{}", prefill_token_count);
                        let _ = write!(arch::serial::Serial, " (token_id=");
                        let _ = write!(arch::serial::Serial, "{}", prompt_token);
                        let _ = writeln!(arch::serial::Serial, "):");
                    }

                    // Forward-pass single token, KV-Cache populated incrementally.
                    // Three dispatches per ADR-029 Phase 3:
                    //   1. fallback: sacred scalar (default mode β-anchor invariant)
                    //   2. aarch64+neon: NEON-accelerated (Sub-MP-E3 path)
                    //   3. x86_64+avx512: AVX-512 parallel-dispatched (ADR-029 Phase 3)
                    // The fallback cfg negates both accel paths so exactly one branch
                    // is selected at compile time.
                    let profile_prompt_fwd_t0 = arch::read_cycles();
                    #[cfg(not(any(
                        all(target_arch = "aarch64", feature = "neon-acceleration"),
                        all(target_arch = "x86_64", feature = "avx512-acceleration"),
                    )))]
                    let fwd_result = zero_llm_inference::forward_single_token::<64>(
                        prompt_token,
                        position,
                        &layer_arr,
                        token_embd,
                        &fp_dispatch,
                        &rope_ctx,
                        rms_eps,
                        embedding_dim,
                        intermediate_dim,
                        n_q_heads,
                        n_kv_heads,
                        head_dim,
                        &mut kv_cache,
                        hidden,
                        norm_buf,
                        attn_out,
                        mlp_out,
                        q_buf,
                        k_buf,
                        v_buf,
                        q_out,
                        k_out,
                        score_buf,
                        attn_head_buf,
                        attn_concat,
                        gate_buf,
                        up_buf,
                        hidden_mlp_buf,
                        &mut lin_scratch,
                    );
                    #[cfg(all(target_arch = "aarch64", feature = "neon-acceleration"))]
                    let fwd_result = unsafe {
                        crate::inference_neon::forward_single_token_neon::<64>(
                            prompt_token,
                            position,
                            &layer_arr,
                            token_embd,
                            &fp_dispatch,
                            &rope_ctx,
                            rms_eps,
                            embedding_dim,
                            intermediate_dim,
                            n_q_heads,
                            n_kv_heads,
                            head_dim,
                            &mut kv_cache,
                            hidden,
                            norm_buf,
                            attn_out,
                            mlp_out,
                            q_buf,
                            k_buf,
                            v_buf,
                            q_out,
                            k_out,
                            score_buf,
                            attn_head_buf,
                            attn_concat,
                            gate_buf,
                            up_buf,
                            hidden_mlp_buf,
                            &mut lin_scratch,
                        )
                    };
                    #[cfg(all(target_arch = "x86_64", feature = "avx512-acceleration"))]
                    let fwd_result = unsafe {
                        crate::inference_avx512::forward_single_token_avx512::<64>(
                            prompt_token,
                            position,
                            &layer_arr,
                            token_embd,
                            &fp_dispatch,
                            &rope_ctx,
                            rms_eps,
                            embedding_dim,
                            intermediate_dim,
                            n_q_heads,
                            n_kv_heads,
                            head_dim,
                            &mut kv_cache,
                            hidden,
                            norm_buf,
                            attn_out,
                            mlp_out,
                            q_buf,
                            k_buf,
                            v_buf,
                            q_out,
                            k_out,
                            score_buf,
                            attn_head_buf,
                            attn_concat,
                            gate_buf,
                            up_buf,
                            hidden_mlp_buf,
                            &mut lin_scratch,
                        )
                    };
                    profile_prefill_forward_cycles = profile_prefill_forward_cycles
                        .wrapping_add(arch::read_cycles().wrapping_sub(profile_prompt_fwd_t0));

                    if let Err(err) = fwd_result {
                        let _ = writeln!(
                            arch::serial::Serial,
                            "[MP3.0] Forward-pass FAILED at prompt token {}: {:?}",
                            i,
                            err
                        );
                        // Mirror the reason onto the held benchmark screen —
                        // serial scrolls away on the Cherry KVM, and a bare
                        // "0.0 tok/s" is undiagnosable from a screen photo.
                        // On the AVX-512 path, prepend the first-non-finite
                        // localisation (e.g. "@L00 q_proj n2048") captured by
                        // the per-step tracer so the single screen photo names
                        // the exact step that went NaN/Inf.
                        #[cfg(all(target_arch = "x86_64", feature = "avx512-acceleration"))]
                        {
                            let mut nfbuf = [0u8; 32];
                            let nflen =
                                crate::inference_avx512::first_nonfinite_detail(&mut nfbuf);
                            let nfstr = core::str::from_utf8(&nfbuf[..nflen]).unwrap_or("");
                            crate::bench::record_llm_failure_args(format_args!(
                                "fwd t{} @{} {:?}",
                                i, nfstr, err
                            ));
                        }
                        #[cfg(all(target_arch = "x86_64", not(feature = "avx512-acceleration")))]
                        crate::bench::record_llm_failure_args(format_args!(
                            "fwd tok {} {:?}",
                            i, err
                        ));
                        phase1_ok = false;
                        break;
                    }

                    if prefill_lm_head_required(i) {
                        // LM-head to get logits (needed for Top-K + β-anchor).
                        // Same three-way dispatch as above.
                        let profile_prompt_lm_t0 = arch::read_cycles();
                        #[cfg(not(any(
                            all(target_arch = "aarch64", feature = "neon-acceleration"),
                            all(target_arch = "x86_64", feature = "avx512-acceleration"),
                        )))]
                        let lm_result = zero_llm_inference::lm_head_argmax_dynamic(
                            hidden,
                            output_norm_w,
                            output_w,
                            output_quant_scalar,
                            rms_eps,
                            embedding_dim,
                            zero_llm_inference::VOCAB_SIZE_PADDED,
                            zero_llm_inference::VOCAB_SIZE_REAL,
                            norm_buf,
                            logits_buf,
                            &mut lin_scratch,
                        );
                        #[cfg(all(target_arch = "aarch64", feature = "neon-acceleration"))]
                        let lm_result = unsafe {
                            crate::inference_neon::lm_head_argmax_neon(
                                hidden,
                                output_norm_w,
                                output_w,
                                output_quant,
                                rms_eps,
                                embedding_dim,
                                norm_buf,
                                logits_buf,
                            )
                        };
                        #[cfg(all(target_arch = "x86_64", feature = "avx512-acceleration"))]
                        let lm_result = unsafe {
                            crate::inference_avx512::lm_head_argmax_avx512(
                                hidden,
                                output_norm_w,
                                output_w,
                                output_quant,
                                rms_eps,
                                embedding_dim,
                                norm_buf,
                                logits_buf,
                            )
                        };
                        profile_prefill_lm_head_cycles = profile_prefill_lm_head_cycles
                            .wrapping_add(arch::read_cycles().wrapping_sub(profile_prompt_lm_t0));
                        match lm_result {
                            Ok(next_token_argmax) => {
                                #[cfg(feature = "streaming-mode")]
                                let _ = next_token_argmax;

                                // Per-token Top-K capture
                                let profile_topk_t0 = arch::read_cycles();
                                capture_top_k_logits(logits_buf);
                                profile_prefill_logit_scan_cycles =
                                    profile_prefill_logit_scan_cycles.wrapping_add(
                                        arch::read_cycles().wrapping_sub(profile_topk_t0),
                                    );

                                // β-anchor verification after first prompt token "Hello"
                                #[cfg(not(feature = "streaming-mode"))]
                                if i == 0 {
                                    // Find Top-1 logit_bits for V2.1 anchor verification
                                    let profile_anchor_scan_t0 = arch::read_cycles();
                                    let mut top1_bits: u32 = 0;
                                    let mut max_val: f32 = f32::NEG_INFINITY;
                                    for &lg in logits_buf.iter() {
                                        if lg > max_val {
                                            max_val = lg;
                                            top1_bits = lg.to_bits();
                                        }
                                    }
                                    profile_prefill_logit_scan_cycles =
                                        profile_prefill_logit_scan_cycles.wrapping_add(
                                            arch::read_cycles()
                                                .wrapping_sub(profile_anchor_scan_t0),
                                        );

                                    // Mirror the measured anchor onto the held benchmark
                                    // screen. The Cherry capture pass reads token +
                                    // logit_bits off the KVM/VGA photo to promote to a
                                    // strict manifest anchor (ADR-029 v3); serial (COM1)
                                    // is unreachable there. Recorded before the verify
                                    // branches so every path (capture/strict/missing/β)
                                    // surfaces its values. Borrow the Option so the
                                    // `if let` below still owns it.
                                    #[cfg(target_arch = "x86_64")]
                                    {
                                        let anchor_mode = match &smodel_validation_anchor {
                                            Some(a) if a.strict => crate::bench::ANCHOR_MODE_STRICT,
                                            Some(_) => crate::bench::ANCHOR_MODE_CAPTURE,
                                            None if native_smodel_profile => {
                                                crate::bench::ANCHOR_MODE_MISSING
                                            }
                                            None => crate::bench::ANCHOR_MODE_BETA,
                                        };
                                        crate::bench::record_llm_anchor(
                                            next_token_argmax,
                                            top1_bits,
                                            anchor_mode,
                                        );
                                    }

                                    if let Some(anchor) = smodel_validation_anchor {
                                        if !anchor.matched_runtime {
                                            let _ = writeln!(
                                                arch::serial::Serial,
                                                "[MP3.0] .smodel ANCHOR FAIL: no matching {}/{} validation anchor — HALTING",
                                                crate::model_loader::smodel_expected_anchor_target_arch(),
                                                crate::model_loader::smodel_expected_anchor_profile()
                                            );
                                            if anchor.strict {
                                                loop {
                                                    core::hint::spin_loop();
                                                }
                                            }
                                        }
                                        let _ = writeln!(
                                            arch::serial::Serial,
                                            "[MP3.0] .smodel anchor verification:"
                                        );
                                        let _ = write!(
                                            arch::serial::Serial,
                                            "[MP3.0]   token = {}",
                                            next_token_argmax
                                        );
                                        if let Some(expected) = anchor.expected_next_token {
                                            let _ = write!(
                                                arch::serial::Serial,
                                                " (expected {})",
                                                expected
                                            );
                                        }
                                        let _ = writeln!(arch::serial::Serial);
                                        let _ = write!(
                                            arch::serial::Serial,
                                            "[MP3.0]   logit_bits = 0x{:08x}",
                                            top1_bits
                                        );
                                        if let Some(expected) = anchor.expected_logit_bits {
                                            let _ = write!(
                                                arch::serial::Serial,
                                                " (expected 0x{:08x})",
                                                expected
                                            );
                                        }
                                        let _ = writeln!(arch::serial::Serial);

                                        if anchor.strict {
                                            if anchor.expected_next_token != Some(next_token_argmax)
                                            {
                                                let _ = writeln!(
                                                    arch::serial::Serial,
                                                    "[MP3.0] .smodel ANCHOR FAIL: Token-ID mismatch — HALTING"
                                                );
                                                loop {
                                                    core::hint::spin_loop();
                                                }
                                            }
                                            if anchor.expected_logit_bits != Some(top1_bits) {
                                                let _ = writeln!(
                                                    arch::serial::Serial,
                                                    "[MP3.0] .smodel ANCHOR FAIL: logit_bits mismatch — HALTING"
                                                );
                                                loop {
                                                    core::hint::spin_loop();
                                                }
                                            }
                                            let _ = writeln!(
                                                arch::serial::Serial,
                                                "[MP3.0] .smodel anchor PASS: manifest chain preserved"
                                            );
                                        } else {
                                            let _ = writeln!(
                                                arch::serial::Serial,
                                                "[MP3.0] .smodel anchor captured: token={} logit_bits=0x{:08x}; manifest mode=capture",
                                                next_token_argmax,
                                                top1_bits
                                            );
                                        }
                                    } else if native_smodel_profile {
                                        let _ = writeln!(
                                            arch::serial::Serial,
                                            "[MP3.0] native .smodel anchor missing: token={} logit_bits=0x{:08x}; strict validation not available",
                                            next_token_argmax,
                                            top1_bits
                                        );
                                    } else {
                                        let _ = writeln!(
                                            arch::serial::Serial,
                                            "[MP3.0] \u{03b2}-anchor verification:"
                                        );
                                        let _ = write!(arch::serial::Serial, "[MP3.0]   token = ");
                                        let _ =
                                            write!(arch::serial::Serial, "{}", next_token_argmax);
                                        let _ = write!(arch::serial::Serial, " (expected ");
                                        let _ = write!(
                                            arch::serial::Serial,
                                            "{}",
                                            BETA_ANCHOR_EXPECTED_NEXT_TOKEN
                                        );
                                        let _ = writeln!(arch::serial::Serial, ")");
                                        let _ = write!(
                                            arch::serial::Serial,
                                            "[MP3.0]   logit_bits = 0x"
                                        );
                                        let _ = write!(arch::serial::Serial, "{:08x}", top1_bits);
                                        let _ = write!(arch::serial::Serial, " (expected 0x");
                                        let _ = write!(
                                            arch::serial::Serial,
                                            "{:08x}",
                                            BETA_ANCHOR_EXPECTED_LOGIT_BITS
                                        );
                                        let _ = writeln!(arch::serial::Serial, ")");

                                        // ADR-029 v3 Two-Anchor Registration:
                                        // Token-ID is HARD GATE (algorithmic correctness).
                                        // logit_bits is diagnostic-only (LLVM context-sensitive
                                        // per E5-V2 empirical discovery, NOT V3.1 mandate per
                                        // Lesson 14 SP2).
                                        if next_token_argmax != BETA_ANCHOR_EXPECTED_NEXT_TOKEN {
                                            let _ =
                                                writeln!(arch::serial::Serial,
                                            "[MP3.0] \u{03b2}-ANCHOR FAIL: Token-ID mismatch — HALTING");
                                            loop {
                                                core::hint::spin_loop();
                                            }
                                        }
                                        if top1_bits != BETA_ANCHOR_EXPECTED_LOGIT_BITS {
                                            let _ = writeln!(arch::serial::Serial,
                                        "[MP3.0] \u{03b2}-anchor WARNING: logit_bits drift (LLVM context-sensitive, documented per ADR-029 v3)");
                                        }

                                        let _ = writeln!(arch::serial::Serial,
                                        "[MP3.0] \u{03b2}-anchor PASS: Sub-MP-C3/C5 V2.1 chain preserved");
                                    }
                                }
                            }
                            // Sub-MP-G1: Streaming-mode allows chat-template special tokens
                            // (151644 = <|im_start|>, 151645 = <|im_end|>) which exceed
                            // VOCAB_SIZE_REAL (151643). Sacred crate lm_head_argmax returns
                            // ArgmaxInPaddingRegion for these — we bypass in kernel only.
                            #[cfg(feature = "streaming-mode")]
                            Err(zero_llm_inference::LmHeadError::ArgmaxInPaddingRegion(idx)) => {
                                let _ = write!(
                                    arch::serial::Serial,
                                    "[G1] Special token in padding region: "
                                );
                                let _ = writeln!(arch::serial::Serial, "{}", idx);
                                // Continue prefill — special tokens are legitimate in chat-template
                            }
                            Err(e) => {
                                let _ = writeln!(
                                    arch::serial::Serial,
                                    "[MP3.0] LM head FAILED at prompt token {}: {:?}",
                                    i,
                                    e
                                );
                                #[cfg(target_arch = "x86_64")]
                                crate::bench::record_llm_failure_args(format_args!(
                                    "lm-head tok {} {:?}",
                                    i, e
                                ));
                                phase1_ok = false;
                                break;
                            }
                        }
                    }
                }

                if !phase1_ok {
                    let _ = writeln!(arch::serial::Serial, "[MP3.0] Phase 1 FAILED — aborting");
                } else {
                    let profile_prefill_wall_cycles =
                        arch::read_cycles().wrapping_sub(profile_prefill_wall_t0);
                    let _ = write!(arch::serial::Serial, "[MP3.0] Phase 1 complete: ");
                    let _ = write!(arch::serial::Serial, "{}", prefill_token_count);
                    let _ = writeln!(arch::serial::Serial, " prefill tokens processed");

                    #[cfg(feature = "streaming-mode")]
                    let generated_start = sequence_len;

                    let generation_wall_t0 = arch::read_cycles();
                    let mut generation_compute_cycles: u64 = 0;
                    let mut generation_forward_cycles: u64 = 0;
                    let mut generation_lm_head_cycles: u64 = 0;
                    #[allow(unused_mut)]
                    let mut generation_sample_cycles: u64 = 0;
                    let mut generation_logit_scan_cycles: u64 = 0;
                    #[allow(unused_mut)]
                    let mut generation_render_cycles: u64 = 0;
                    #[cfg(all(target_arch = "x86_64", feature = "avx512-acceleration"))]
                    let avx512_perf_before = crate::inference_avx512::perf_counters_snapshot();

                    // Production prefill only runs LM-head for the beta anchor.
                    // Compute the first generated token here from the final prompt
                    // hidden state. debug-logits builds may still run per-token
                    // prefill LM-head above for continuous logits inspection.
                    // ADR-029 Phase 3: three-way dispatch as in Phase 1.
                    let first_lm_t0 = arch::read_cycles();
                    #[cfg(not(any(
                        all(target_arch = "aarch64", feature = "neon-acceleration"),
                        all(target_arch = "x86_64", feature = "avx512-acceleration"),
                    )))]
                    let first_gen = zero_llm_inference::lm_head_argmax_dynamic(
                        hidden,
                        output_norm_w,
                        output_w,
                        output_quant_scalar,
                        rms_eps,
                        embedding_dim,
                        zero_llm_inference::VOCAB_SIZE_PADDED,
                        zero_llm_inference::VOCAB_SIZE_REAL,
                        norm_buf,
                        logits_buf,
                        &mut lin_scratch,
                    );
                    #[cfg(all(target_arch = "aarch64", feature = "neon-acceleration"))]
                    let first_gen = unsafe {
                        crate::inference_neon::lm_head_argmax_neon(
                            hidden,
                            output_norm_w,
                            output_w,
                            output_quant,
                            rms_eps,
                            embedding_dim,
                            norm_buf,
                            logits_buf,
                        )
                    };
                    #[cfg(all(target_arch = "x86_64", feature = "avx512-acceleration"))]
                    let first_gen = unsafe {
                        crate::inference_avx512::lm_head_argmax_avx512(
                            hidden,
                            output_norm_w,
                            output_w,
                            output_quant,
                            rms_eps,
                            embedding_dim,
                            norm_buf,
                            logits_buf,
                        )
                    };
                    let first_lm_cycles = arch::read_cycles().wrapping_sub(first_lm_t0);
                    generation_lm_head_cycles =
                        generation_lm_head_cycles.wrapping_add(first_lm_cycles);
                    generation_compute_cycles =
                        generation_compute_cycles.wrapping_add(first_lm_cycles);

                    // Sub-MP-G1: Extract first generated token, handling special tokens
                    #[cfg(not(feature = "streaming-mode"))]
                    let first_gen_opt: Option<u32> = first_gen.ok();
                    #[cfg(feature = "streaming-mode")]
                    let first_gen_opt: Option<u32> = {
                        let _ = first_gen;
                        let boot_tick = arch::read_cycles();
                        let prompt_u8: &[u8] = unsafe {
                            core::slice::from_raw_parts(
                                PROMPT_TOKENS.as_ptr() as *const u8,
                                PROMPT_TOKEN_COUNT * 4,
                            )
                        };
                        let seed =
                            crate::rng::entropy_seed(boot_tick, dream_session_id as u32, prompt_u8);
                        let mut early_rng = crate::rng::Rng::new(seed);
                        let sample_t0 = arch::read_cycles();
                        let sampled =
                            crate::sampler::lm_head_sample(logits_buf, &mut early_rng, 0.7);
                        generation_sample_cycles = generation_sample_cycles
                            .wrapping_add(arch::read_cycles().wrapping_sub(sample_t0));
                        let _ = write!(arch::serial::Serial, "[G2] First gen sampled token=");
                        let _ = writeln!(arch::serial::Serial, "{}", sampled);
                        Some(sampled)
                    };

                    if let Some(first_gen_token) = first_gen_opt {
                        // Store first generated token
                        sequence[sequence_len] = first_gen_token;
                        sequence_len += 1;

                        let _ = write!(
                            arch::serial::Serial,
                            "[MP3.0] Phase 2: Autoregressive generation (max "
                        );
                        let _ = write!(arch::serial::Serial, "{}", MAX_GENERATED_TOKENS);
                        let _ = writeln!(arch::serial::Serial, " tokens)");

                        if stage11_verbose_logging() {
                            let _ = write!(arch::serial::Serial, "[MP3.0] Generated token 1/");
                            let _ = write!(arch::serial::Serial, "{}", MAX_GENERATED_TOKENS);
                            let _ = write!(arch::serial::Serial, " (token_id=");
                            let _ = write!(arch::serial::Serial, "{}", first_gen_token);
                            let _ = writeln!(arch::serial::Serial, "):");
                        }
                        // Top-K already captured in Phase 1's last iteration

                        // Sub-MP-F2: Typewriter live-render for first generated token
                        // Sub-MP-G1 M7: cfg-gated to aarch64 (ramfb only)
                        #[cfg(target_arch = "aarch64")]
                        unsafe {
                            let render_t0 = arch::read_cycles();
                            let decoded = detokenizer::decode_token(first_gen_token);
                            crate::lfb::typewriter::push_text(decoded);
                            generation_render_cycles = generation_render_cycles
                                .wrapping_add(arch::read_cycles().wrapping_sub(render_t0));
                        }

                        let mut generated_count: usize = 1;
                        let mut eos_stopped = false;

                        // Sub-MP-D3.5: Initialize RNG for non-deterministic sampling
                        // (feature-gated, default OFF per SQ1=β)
                        #[cfg(feature = "nondeterministic-sampling")]
                        let mut sampling_rng = {
                            // Entropy seed: state-derived hash per SQ2=γ
                            // boot_uptime_tick from arch::read_cycles() (HAL-abstracted,
                            // aarch64=CNTVCT_EL0, x86_64=rdtsc — Pillar 7 satisfied
                            // because platform-specific code is in existing arch/ HAL)
                            let boot_tick = arch::read_cycles();
                            // Prompt bytes for entropy mixing (safe byte-slice,
                            // Sub-MP-G1: supports both 13-token and 50-token prompts)
                            let prompt_u8: &[u8] = unsafe {
                                core::slice::from_raw_parts(
                                    PROMPT_TOKENS.as_ptr() as *const u8,
                                    PROMPT_TOKEN_COUNT * 4,
                                )
                            };
                            let seed = crate::rng::entropy_seed(
                                boot_tick,
                                dream_session_id as u32,
                                prompt_u8,
                            );
                            let _ = write!(
                                arch::serial::Serial,
                                "[MP3.5] Non-deterministic sampling enabled (seed="
                            );
                            let _ = write!(arch::serial::Serial, "{}", seed);
                            #[cfg(feature = "streaming-mode")]
                            let _ = writeln!(arch::serial::Serial, ", Top-K=40, Temp=0.7)");
                            #[cfg(not(feature = "streaming-mode"))]
                            let _ = writeln!(arch::serial::Serial, ", Top-K=40, Temp=1.0)");
                            crate::rng::Rng::new(seed)
                        };

                        // Check EOS on first generated token
                        if first_gen_token == EOS_TOKEN_ENDOFTEXT
                            || first_gen_token == EOS_TOKEN_IM_END
                        {
                            let _ = write!(arch::serial::Serial, "[MP3.0] EOS detected (token_id=");
                            let _ = write!(arch::serial::Serial, "{}", first_gen_token);
                            let _ = writeln!(arch::serial::Serial, ") at generated[0], early stop");
                            eos_stopped = true;
                        }

                        // Generate remaining tokens
                        while !eos_stopped && generated_count < MAX_GENERATED_TOKENS {
                            let current_token = sequence[sequence_len - 1];
                            let position = sequence_len - 1;
                            // position = index where this token's KV gets stored.
                            // sequence_len-1 because we already incremented after storing.

                            // Forward-pass: run current_token through 28 layers
                            // Sub-MP-F2: Reset layer progress bar for new token
                            // Sub-MP-G1 M7: cfg-gated to aarch64 (ramfb only)
                            #[cfg(target_arch = "aarch64")]
                            unsafe {
                                crate::lfb::layer_progress::reset();
                            }
                            // ADR-029 Phase 3: three-way dispatch.
                            let fwd_t0 = arch::read_cycles();
                            #[cfg(not(any(
                                all(target_arch = "aarch64", feature = "neon-acceleration"),
                                all(target_arch = "x86_64", feature = "avx512-acceleration"),
                            )))]
                            let fwd_result = zero_llm_inference::forward_single_token::<64>(
                                current_token,
                                position,
                                &layer_arr,
                                token_embd,
                                &fp_dispatch,
                                &rope_ctx,
                                rms_eps,
                                embedding_dim,
                                intermediate_dim,
                                n_q_heads,
                                n_kv_heads,
                                head_dim,
                                &mut kv_cache,
                                hidden,
                                norm_buf,
                                attn_out,
                                mlp_out,
                                q_buf,
                                k_buf,
                                v_buf,
                                q_out,
                                k_out,
                                score_buf,
                                attn_head_buf,
                                attn_concat,
                                gate_buf,
                                up_buf,
                                hidden_mlp_buf,
                                &mut lin_scratch,
                            );
                            #[cfg(all(target_arch = "aarch64", feature = "neon-acceleration"))]
                            let fwd_result = unsafe {
                                crate::inference_neon::forward_single_token_neon::<64>(
                                    current_token,
                                    position,
                                    &layer_arr,
                                    token_embd,
                                    &fp_dispatch,
                                    &rope_ctx,
                                    rms_eps,
                                    embedding_dim,
                                    intermediate_dim,
                                    n_q_heads,
                                    n_kv_heads,
                                    head_dim,
                                    &mut kv_cache,
                                    hidden,
                                    norm_buf,
                                    attn_out,
                                    mlp_out,
                                    q_buf,
                                    k_buf,
                                    v_buf,
                                    q_out,
                                    k_out,
                                    score_buf,
                                    attn_head_buf,
                                    attn_concat,
                                    gate_buf,
                                    up_buf,
                                    hidden_mlp_buf,
                                    &mut lin_scratch,
                                )
                            };
                            #[cfg(all(target_arch = "x86_64", feature = "avx512-acceleration"))]
                            let fwd_result = unsafe {
                                crate::inference_avx512::forward_single_token_avx512::<64>(
                                    current_token,
                                    position,
                                    &layer_arr,
                                    token_embd,
                                    &fp_dispatch,
                                    &rope_ctx,
                                    rms_eps,
                                    embedding_dim,
                                    intermediate_dim,
                                    n_q_heads,
                                    n_kv_heads,
                                    head_dim,
                                    &mut kv_cache,
                                    hidden,
                                    norm_buf,
                                    attn_out,
                                    mlp_out,
                                    q_buf,
                                    k_buf,
                                    v_buf,
                                    q_out,
                                    k_out,
                                    score_buf,
                                    attn_head_buf,
                                    attn_concat,
                                    gate_buf,
                                    up_buf,
                                    hidden_mlp_buf,
                                    &mut lin_scratch,
                                )
                            };
                            let fwd_cycles = arch::read_cycles().wrapping_sub(fwd_t0);
                            generation_forward_cycles =
                                generation_forward_cycles.wrapping_add(fwd_cycles);
                            generation_compute_cycles =
                                generation_compute_cycles.wrapping_add(fwd_cycles);

                            if fwd_result.is_err() {
                                let _ = write!(
                                    arch::serial::Serial,
                                    "[MP3.0] Forward-pass FAILED at generated token "
                                );
                                let _ = writeln!(arch::serial::Serial, "{}", generated_count);
                                break;
                            }

                            // LM-head argmax → next token (ADR-029 Phase 3 three-way dispatch).
                            let lm_t0 = arch::read_cycles();
                            #[cfg(not(any(
                                all(target_arch = "aarch64", feature = "neon-acceleration"),
                                all(target_arch = "x86_64", feature = "avx512-acceleration"),
                            )))]
                            let lm_result2 = zero_llm_inference::lm_head_argmax_dynamic(
                                hidden,
                                output_norm_w,
                                output_w,
                                output_quant_scalar,
                                rms_eps,
                                embedding_dim,
                                zero_llm_inference::VOCAB_SIZE_PADDED,
                                zero_llm_inference::VOCAB_SIZE_REAL,
                                norm_buf,
                                logits_buf,
                                &mut lin_scratch,
                            );
                            #[cfg(all(target_arch = "aarch64", feature = "neon-acceleration"))]
                            let lm_result2 = unsafe {
                                crate::inference_neon::lm_head_argmax_neon(
                                    hidden,
                                    output_norm_w,
                                    output_w,
                                    output_quant,
                                    rms_eps,
                                    embedding_dim,
                                    norm_buf,
                                    logits_buf,
                                )
                            };
                            #[cfg(all(target_arch = "x86_64", feature = "avx512-acceleration"))]
                            let lm_result2 = unsafe {
                                crate::inference_avx512::lm_head_argmax_avx512(
                                    hidden,
                                    output_norm_w,
                                    output_w,
                                    output_quant,
                                    rms_eps,
                                    embedding_dim,
                                    norm_buf,
                                    logits_buf,
                                )
                            };
                            let lm_cycles = arch::read_cycles().wrapping_sub(lm_t0);
                            generation_lm_head_cycles =
                                generation_lm_head_cycles.wrapping_add(lm_cycles);
                            generation_compute_cycles =
                                generation_compute_cycles.wrapping_add(lm_cycles);
                            match lm_result2 {
                                #[allow(unused_variables)]
                                Ok(next_token_argmax) => {
                                    // Per-token Top-K capture
                                    let topk_t0 = arch::read_cycles();
                                    capture_top_k_logits(logits_buf);
                                    generation_logit_scan_cycles = generation_logit_scan_cycles
                                        .wrapping_add(arch::read_cycles().wrapping_sub(topk_t0));

                                    // Sub-MP-D3.5: cfg-feature dispatch
                                    // Default (no feature): use argmax result (sacred path)
                                    // Feature enabled: sample from logits_buf instead
                                    #[cfg(not(feature = "nondeterministic-sampling"))]
                                    let next_token = next_token_argmax;

                                    #[cfg(feature = "nondeterministic-sampling")]
                                    let next_token = {
                                        // Sub-MP-G1: temperature 0.7 for streaming-mode
                                        // per design specification (was 1.0 SQ3=α).
                                        #[cfg(feature = "streaming-mode")]
                                        const SAMPLE_TEMP: f32 = 0.7;
                                        #[cfg(not(feature = "streaming-mode"))]
                                        const SAMPLE_TEMP: f32 = 1.0;
                                        let sample_t0 = arch::read_cycles();
                                        let sampled = crate::sampler::lm_head_sample(
                                            logits_buf,
                                            &mut sampling_rng,
                                            SAMPLE_TEMP,
                                        );
                                        generation_sample_cycles = generation_sample_cycles
                                            .wrapping_add(
                                                arch::read_cycles().wrapping_sub(sample_t0),
                                            );
                                        sampled
                                    };

                                    sequence[sequence_len] = next_token;
                                    sequence_len += 1;
                                    generated_count += 1;

                                    if stage11_verbose_logging() {
                                        let _ = write!(
                                            arch::serial::Serial,
                                            "[MP3.0] Generated token "
                                        );
                                        let _ = write!(arch::serial::Serial, "{}", generated_count);
                                        let _ = write!(arch::serial::Serial, "/");
                                        let _ = write!(
                                            arch::serial::Serial,
                                            "{}",
                                            MAX_GENERATED_TOKENS
                                        );
                                        let _ = write!(arch::serial::Serial, " (token_id=");
                                        let _ = write!(arch::serial::Serial, "{}", next_token);
                                        let _ = writeln!(arch::serial::Serial, "):");
                                    }

                                    // Sub-MP-F2: Typewriter live-render for generated token
                                    // Sub-MP-G1 M7: cfg-gated to aarch64 (ramfb only)
                                    #[cfg(target_arch = "aarch64")]
                                    unsafe {
                                        let render_t0 = arch::read_cycles();
                                        let decoded = detokenizer::decode_token(next_token);
                                        crate::lfb::typewriter::push_text(decoded);
                                        generation_render_cycles = generation_render_cycles
                                            .wrapping_add(
                                                arch::read_cycles().wrapping_sub(render_t0),
                                            );
                                    }

                                    // EOS detection
                                    if next_token == EOS_TOKEN_ENDOFTEXT
                                        || next_token == EOS_TOKEN_IM_END
                                    {
                                        let _ = write!(
                                            arch::serial::Serial,
                                            "[MP3.0] EOS detected (token_id="
                                        );
                                        let _ = write!(arch::serial::Serial, "{}", next_token);
                                        let _ = write!(arch::serial::Serial, ") at generated[");
                                        let _ =
                                            write!(arch::serial::Serial, "{}", generated_count - 1);
                                        let _ = writeln!(arch::serial::Serial, "], early stop");
                                        eos_stopped = true;
                                    }
                                }
                                // Sub-MP-G1: Streaming-mode chat-template special token bypass
                                #[cfg(feature = "streaming-mode")]
                                Err(zero_llm_inference::LmHeadError::ArgmaxInPaddingRegion(
                                    _idx,
                                )) => {
                                    // Sampler operates on full logits_buf which includes
                                    // special token positions — let it sample normally.
                                    let topk_t0 = arch::read_cycles();
                                    capture_top_k_logits(logits_buf);
                                    generation_logit_scan_cycles = generation_logit_scan_cycles
                                        .wrapping_add(arch::read_cycles().wrapping_sub(topk_t0));
                                    let next_token = {
                                        #[cfg(feature = "streaming-mode")]
                                        const SAMPLE_TEMP: f32 = 0.7;
                                        let sample_t0 = arch::read_cycles();
                                        let sampled = crate::sampler::lm_head_sample(
                                            logits_buf,
                                            &mut sampling_rng,
                                            SAMPLE_TEMP,
                                        );
                                        generation_sample_cycles = generation_sample_cycles
                                            .wrapping_add(
                                                arch::read_cycles().wrapping_sub(sample_t0),
                                            );
                                        sampled
                                    };

                                    sequence[sequence_len] = next_token;
                                    sequence_len += 1;
                                    generated_count += 1;

                                    if stage11_verbose_logging() {
                                        let _ = write!(
                                            arch::serial::Serial,
                                            "[MP3.0] Generated token "
                                        );
                                        let _ = write!(arch::serial::Serial, "{}", generated_count);
                                        let _ = write!(arch::serial::Serial, "/");
                                        let _ = write!(
                                            arch::serial::Serial,
                                            "{}",
                                            MAX_GENERATED_TOKENS
                                        );
                                        let _ = write!(arch::serial::Serial, " (token_id=");
                                        let _ = write!(arch::serial::Serial, "{}", next_token);
                                        let _ = writeln!(
                                            arch::serial::Serial,
                                            ") [special-token-bypass]:"
                                        );
                                    }

                                    #[cfg(target_arch = "aarch64")]
                                    unsafe {
                                        let render_t0 = arch::read_cycles();
                                        let decoded = detokenizer::decode_token(next_token);
                                        crate::lfb::typewriter::push_text(decoded);
                                        generation_render_cycles = generation_render_cycles
                                            .wrapping_add(
                                                arch::read_cycles().wrapping_sub(render_t0),
                                            );
                                    }

                                    // EOS detection (special tokens include im_end)
                                    if next_token == EOS_TOKEN_ENDOFTEXT
                                        || next_token == EOS_TOKEN_IM_END
                                    {
                                        let _ = write!(
                                            arch::serial::Serial,
                                            "[MP3.0] EOS detected (token_id="
                                        );
                                        let _ = write!(arch::serial::Serial, "{}", next_token);
                                        let _ = writeln!(
                                            arch::serial::Serial,
                                            ") via special-token-bypass, early stop"
                                        );
                                        eos_stopped = true;
                                    }
                                }
                                Err(_) => {
                                    let _ = write!(
                                        arch::serial::Serial,
                                        "[MP3.0] LM head FAILED at generated token "
                                    );
                                    let _ = writeln!(arch::serial::Serial, "{}", generated_count);
                                    break;
                                }
                            }
                        }

                        // Phase 2 also handles Phase 1's first-gen token stored above,
                        // then continues.

                        let generation_wall_cycles =
                            arch::read_cycles().wrapping_sub(generation_wall_t0);
                        #[cfg(target_arch = "x86_64")]
                        crate::bench::record_llm_inference(
                            generated_count as u64,
                            generation_compute_cycles,
                        );
                        #[cfg(target_arch = "x86_64")]
                        crate::control_plane::record_llm_profile(
                            crate::control_plane::LlmProfile {
                                valid: true,
                                run_id: 0,
                                prompt_tokens: prefill_token_count as u64,
                                generated_tokens: generated_count as u64,
                                prefill_wall_cycles: profile_prefill_wall_cycles,
                                prefill_forward_cycles: profile_prefill_forward_cycles,
                                prefill_lm_head_cycles: profile_prefill_lm_head_cycles,
                                prefill_logit_scan_cycles: profile_prefill_logit_scan_cycles,
                                generation_wall_cycles,
                                generation_compute_cycles,
                                generation_forward_cycles,
                                generation_lm_head_cycles,
                                generation_sample_cycles,
                                generation_logit_scan_cycles,
                                generation_render_cycles,
                            },
                        );
                        let _ = writeln!(
                            arch::serial::Serial,
                            "[MP3.0] Generation compute timing: tokens={} compute_cycles={} wall_cycles={} forward_cycles={} lm_head_cycles={} sample_cycles={} logit_scan_cycles={} render_cycles={}",
                            generated_count,
                            generation_compute_cycles,
                            generation_wall_cycles,
                            generation_forward_cycles,
                            generation_lm_head_cycles,
                            generation_sample_cycles,
                            generation_logit_scan_cycles,
                            generation_render_cycles
                        );
                        #[cfg(all(target_arch = "x86_64", feature = "avx512-acceleration"))]
                        {
                            let avx512_perf_after =
                                crate::inference_avx512::perf_counters_snapshot();
                            let avx512_delta = avx512_perf_after.delta_since(avx512_perf_before);
                            let _ = writeln!(
                                arch::serial::Serial,
                                "[MP3.0] AVX-512 generation profile: q4k_calls={} q4k_cycles={} q6k_calls={} q6k_cycles={} lm_head_calls={} lm_head_cycles={} fused_calls={} fused_cycles={} attn_par_calls={} attn_par_cycles={}",
                                avx512_delta.q4k_calls,
                                avx512_delta.q4k_cycles,
                                avx512_delta.q6k_calls,
                                avx512_delta.q6k_cycles,
                                avx512_delta.lm_head_calls,
                                avx512_delta.lm_head_cycles,
                                avx512_delta.fused_calls,
                                avx512_delta.fused_cycles,
                                avx512_delta.attn_par_calls,
                                avx512_delta.attn_par_cycles
                            );
                        }

                        #[cfg(feature = "streaming-mode")]
                        {
                            unsafe {
                                commit_dream_memory(
                                    &sequence[..sequence_len],
                                    generated_start,
                                    sequence_len,
                                );
                            }
                            let _ = write!(arch::serial::Serial, "[G2] Eternal Dream session ");
                            let _ = write!(arch::serial::Serial, "{}", dream_session_id);
                            let _ = write!(arch::serial::Serial, " complete: ");
                            let _ = write!(arch::serial::Serial, "{}", generated_count);
                            let _ =
                                write!(arch::serial::Serial, " tokens generated, rolling memory=");
                            let _ = writeln!(arch::serial::Serial, "{}", STREAMING_MEMORY_TOKENS);
                        }

                        #[cfg(not(feature = "streaming-mode"))]
                        {
                            // ─── PHASE 3: Final sequence dump ───
                            let _ = write!(arch::serial::Serial, "[MP3.0] Phase 2 complete: ");
                            let _ = write!(arch::serial::Serial, "{}", generated_count);
                            let _ = write!(arch::serial::Serial, " tokens generated, EOS-stopped=");
                            let _ = writeln!(arch::serial::Serial, "{}", eos_stopped);

                            let _ = write!(arch::serial::Serial, "[MP3.0] Final sequence (");
                            let _ = write!(arch::serial::Serial, "{}", sequence_len);
                            let _ = writeln!(arch::serial::Serial, " tokens total):");

                            let _ = write!(arch::serial::Serial, "[MP3.0] Prompt:    [");
                            for j in 0..PROMPT_TOKEN_COUNT {
                                let _ = write!(arch::serial::Serial, "{}", sequence[j]);
                                if j + 1 < PROMPT_TOKEN_COUNT {
                                    let _ = write!(arch::serial::Serial, ", ");
                                }
                            }
                            let _ = writeln!(arch::serial::Serial, "]");

                            let _ = write!(arch::serial::Serial, "[MP3.0] Generated: [");
                            for j in PROMPT_TOKEN_COUNT..sequence_len {
                                let _ = write!(arch::serial::Serial, "{}", sequence[j]);
                                if j + 1 < sequence_len {
                                    let _ = write!(arch::serial::Serial, ", ");
                                }
                            }
                            let _ = writeln!(arch::serial::Serial, "]");

                            let _ = writeln!(
                                arch::serial::Serial,
                                "[MP3.0] Sub-MP-D3 autoregressive generation COMPLETE"
                            );

                            // ─── PHASE 4: BPE Detokenization (Sub-MP-D4) ───
                            //
                            // Design decision: Pillar 7 strict.
                            // Per V3.1 Phase 4 Status (Z.560-571): token-ID → string.
                            // Per V3.1 Pillar 8: Rust temporary, Quarks Stage 12+.

                            let _ = writeln!(
                                arch::serial::Serial,
                                "[MP4.0] Phase 4: BPE Detokenization"
                            );
                            let _ = write!(arch::serial::Serial, "[MP4.0] Vocab size: ");
                            let _ = writeln!(
                                arch::serial::Serial,
                                "{} (compile-time={}, runtime_installed={})",
                                detokenizer::effective_vocab_size(),
                                detokenizer::DETOK_VOCAB_SIZE,
                                detokenizer::has_runtime_vocab(),
                            );

                            // Per-token decoded output is a verification surface for
                            // debug builds. Cherry production keeps the final
                            // concatenated output below but avoids one COM1/KVM
                            // line per token before the benchmark screen.
                            if stage11_verbose_logging() {
                                for idx in 0..sequence_len {
                                    let tid = sequence[idx];
                                    let decoded = detokenizer::decode_token(tid);
                                    let _ = write!(arch::serial::Serial, "[MP4.0]   Token ");
                                    let _ = write!(arch::serial::Serial, "{}", idx);
                                    let _ = write!(arch::serial::Serial, "/");
                                    let _ = write!(arch::serial::Serial, "{}", sequence_len);
                                    let _ = write!(arch::serial::Serial, " (id=");
                                    let _ = write!(arch::serial::Serial, "{}", tid);
                                    let _ = write!(arch::serial::Serial, "): ");
                                    if let Ok(s) = core::str::from_utf8(decoded) {
                                        let _ = write!(arch::serial::Serial, "{}", s);
                                    } else {
                                        let _ = write!(arch::serial::Serial, "<non-utf8>");
                                    }
                                    let _ = writeln!(arch::serial::Serial, "");
                                }
                            }

                            // Final concatenated human-readable output
                            let _ = writeln!(arch::serial::Serial, "");
                            let _ = writeln!(
                                arch::serial::Serial,
                                "[MP4.0] ═══════════════════════════════"
                            );
                            let _ = writeln!(arch::serial::Serial, "[MP4.0] Detokenized output:");
                            let _ = writeln!(
                                arch::serial::Serial,
                                "[MP4.0] ═══════════════════════════════"
                            );
                            for idx in 0..sequence_len {
                                let tid = sequence[idx];
                                let decoded = detokenizer::decode_token(tid);
                                if let Ok(s) = core::str::from_utf8(decoded) {
                                    let _ = write!(arch::serial::Serial, "{}", s);
                                }
                            }
                            let _ = writeln!(arch::serial::Serial, "");
                            let _ = writeln!(
                                arch::serial::Serial,
                                "[MP4.0] ═══════════════════════════════"
                            );
                            let _ = writeln!(
                                arch::serial::Serial,
                                "[MP4.0] Sub-MP-D4 detokenization COMPLETE"
                            );
                        }
                    } else {
                        let _ = writeln!(
                            arch::serial::Serial,
                            "[MP3.0] LM head FAILED for first generated token"
                        );
                        #[cfg(target_arch = "x86_64")]
                        crate::bench::record_llm_failure("lm-head failed for first gen token");
                    }
                }
            } else {
                let _ = writeln!(
                    arch::serial::Serial,
                    "[MP2.5] ACTIVATION_ARENA alloc failed"
                );
            }
        } else {
            let _ = writeln!(
                arch::serial::Serial,
                "[MP2.5] ACTIVATION_ARENA not initialized"
            );
        }
    } else {
        let _ = writeln!(
            arch::serial::Serial,
            "[MP2.5] Missing token_embd/output tensors"
        );
    }
}

// ───────────────────────────────────────────────────────────────────
//  DeepSeek-V2 / Kimi K2.6 forward pass
// ───────────────────────────────────────────────────────────────────
//
// Entry point invoked by `run_forward_pass` when
// `cfg.is_deepseek2() == true`. Kimi K2.6 specifics:
//   * 61 transformer blocks (dynamic from ModelConfig.block_count)
//   * MLA attention (q_lora_rank=1536, kv_lora_rank=512, qk_nope=128,
//     qk_rope=64, v_head=128)
//   * MoE FFN with 384 experts, top-8 + 1 shared expert per layer
//   * vocab_size read from ModelConfig.vocab_size (deepseek2.vocab_size
//     in GGUF metadata) — Kimi K2.6 ships 163_840 (tiktoken-family),
//     DeepSeek-V3 ships 129_280, smaller checkpoints vary. NEVER
//     hardcoded — that's a recipe for silent vocab mismatch on a
//     different model.
//   * Stage-12 NVMe model loader (stub) — until residency lands the
//     boot path detects an unmapped weight region and skips inference.
//
// All scratch is caller-allocated via the activation arena. Per Pillar 1
// no allocation happens on the hot path. Per ADR-029 D13 every dimension
// comes from `ModelConfig`; nothing hard-coded.
fn run_forward_pass_deepseek2(model_bytes: &[u8], tensor_index: &zero_gguf_parser::TensorIndex) {
    // ADR-037: native `.smodel` artifacts carry validation anchors in
    // the SilicatePack manifest. The DeepSeek/Kimi architecture path must
    // enforce the same manifest chain as the Qwen path; GGUF remains a
    // legacy/debug input without native anchors.
    let native_smodel_profile = matches!(model_bytes.get(0..4), Some(magic) if magic == b"SILM");
    let smodel_validation_anchor = if native_smodel_profile {
        match crate::model_loader::smodel_validation_anchor(model_bytes) {
            Ok(anchor) => anchor,
            Err(_) => {
                let _ = writeln!(
                    arch::serial::Serial,
                    "[MoE] .smodel validation anchor parse FAILED — HALTING"
                );
                loop {
                    core::hint::spin_loop();
                }
            }
        }
    } else {
        None
    };

    let cfg = &tensor_index.model_config;
    let n_layers = cfg.block_count as usize;
    let embedding_dim = cfg.embedding_length as usize;
    let feed_forward_length = cfg.feed_forward_length as usize;
    let n_heads = cfg.head_count as usize;
    // DeepSeek-V2 standard-MHA layers use head_count_kv for GQA. The MLA
    // path doesn't need it (KV is shared across all n_heads via the LoRA
    // expansion). When the GGUF doesn't ship head_count_kv (rare), fall
    // back to n_heads (standard MHA without GQA).
    let n_kv_heads = if cfg.head_count_kv > 0 {
        cfg.head_count_kv as usize
    } else {
        n_heads
    };
    let rms_eps = cfg.layer_norm_rms_epsilon;
    let rope_freq_base = cfg.rope_freq_base;

    // SPEC: DeepSeek-V3 / Kimi K2.6 router uses sigmoid + sum-normalise
    // (not softmax) over the top-K expert scores; using softmax instead
    // systematically skews routing and degrades quality. Pinned here so
    // both prefill and generation forward passes agree.
    let routing_mode = zero_llm_inference::MoeRoutingMode::SigmoidNormalize;
    let expert_weight_scale = match cfg.expert_weights_scale {
        Some(scale) => scale,
        None => {
            // A MoE DeepSeek2 artifact without a routed scale is almost
            // certainly a stale pack: Kimi K2.6 ships 2.827, and silently
            // running with 1.0 degrades every routed expert contribution
            // without any visible failure. Warn loudly; 1.0 remains the
            // only sane value for genuinely scale-free configs.
            if cfg.expert_count.map_or(false, |c| c > 0) {
                let _ = writeln!(
                    arch::serial::Serial,
                    "[MoE] WARNING: expert_weights_scale missing from artifact; \
                     falling back to 1.0 (Kimi K2.6 expects 2.827 — repack with \
                     a SilicatePack version that writes the SIDX scale field)"
                );
            }
            1.0
        }
    };

    let _ = writeln!(
        arch::serial::Serial,
        "[MoE] entered run_forward_pass_deepseek2 — extracting MLA config"
    );
    let _ = writeln!(
        arch::serial::Serial,
        "[MoE] cfg: kv_lora={:?} q_lora={:?} qk_rope={:?} qk_nope={:?} v_head={:?}",
        cfg.kv_lora_rank,
        cfg.q_lora_rank,
        cfg.qk_rope_head_dim,
        cfg.qk_nope_head_dim,
        cfg.v_head_dim
    );

    let kv_lora_rank = match cfg.kv_lora_rank {
        Some(v) => v as usize,
        None => {
            let _ = writeln!(
                arch::serial::Serial,
                "[MoE] missing attention.kv_lora_rank — ABORT"
            );
            return;
        }
    };
    let q_lora_rank = match cfg.q_lora_rank {
        Some(v) => v as usize,
        None => {
            let _ = writeln!(arch::serial::Serial, "[MoE] missing attention.q_lora_rank");
            return;
        }
    };
    let qk_nope_head_dim = match cfg.qk_nope_head_dim {
        Some(v) => v as usize,
        None => {
            let _ = writeln!(
                arch::serial::Serial,
                "[MoE] missing attention.qk_nope_head_dim"
            );
            return;
        }
    };
    let qk_rope_head_dim = match cfg.qk_rope_head_dim {
        Some(v) => v as usize,
        None => {
            let _ = writeln!(
                arch::serial::Serial,
                "[MoE] missing attention.qk_rope_head_dim"
            );
            return;
        }
    };
    let v_head_dim = match cfg.v_head_dim {
        Some(v) => v as usize,
        None => {
            let _ = writeln!(arch::serial::Serial, "[MoE] missing attention.v_head_dim");
            return;
        }
    };

    let n_experts = cfg.expert_count.unwrap_or(0) as usize;
    let top_k = cfg.expert_used_count.unwrap_or(0) as usize;
    let expert_intermediate = cfg.expert_feed_forward_length.unwrap_or(0) as usize;
    let vocab_size_padded = cfg.vocab_size.unwrap_or(0) as usize;

    let _ = write!(arch::serial::Serial, "[MoE] n_experts=");
    let _ = write!(arch::serial::Serial, "{}", n_experts);
    let _ = write!(arch::serial::Serial, " top_k=");
    let _ = write!(arch::serial::Serial, "{}", top_k);
    let _ = write!(arch::serial::Serial, " expert_intermediate=");
    let _ = write!(arch::serial::Serial, "{}", expert_intermediate);
    let _ = write!(arch::serial::Serial, " vocab_size=");
    let _ = writeln!(arch::serial::Serial, "{}", vocab_size_padded);

    if vocab_size_padded == 0 {
        let _ = writeln!(arch::serial::Serial, "[MoE] missing vocab_size — abort");
        return;
    }
    // MoE layers require top_k > 0; if the GGUF lacks expert_used_count
    // the forward pass would hit `assert!(top_k > 0)` deep inside
    // moe_ffn and panic without visible serial output. Catch early.
    if n_experts > 0 && top_k == 0 {
        let _ = writeln!(
            arch::serial::Serial,
            "[MoE] expert_count > 0 but expert_used_count missing or 0 — abort"
        );
        return;
    }
    if n_experts > 0 && expert_intermediate == 0 {
        let _ = writeln!(
            arch::serial::Serial,
            "[MoE] expert_count > 0 but expert_feed_forward_length missing or 0 — abort"
        );
        return;
    }
    let vocab_size_real = vocab_size_padded;

    // ── Stub NVMe-loader check ──────────────────────────────────────
    //
    // Until the NVMe driver lands, large deepseek2 models are
    // typically not resident in `model_bytes`. Detect via tensor-data
    // region exceeding the supplied window and back off gracefully.
    if tensor_index.tensor_data_offset >= model_bytes.len() {
        let _ = writeln!(
            arch::serial::Serial,
            "[MoE] weights not resident — NVMe loader stub. Skipping inference."
        );
        return;
    }

    let _ = writeln!(arch::serial::Serial, "[DBG] after config");
    let _ = writeln!(
        arch::serial::Serial,
        "[MoE] deepseek2 init: building dispatch + weights"
    );

    // ── Per-layer MlpType dispatch (Dense vs MoE) ───────────────────
    //
    // Presence of `ffn_gate_exps.weight` flips a layer Dense → MoE.
    // Kimi K2.6 typically uses Dense for layer 0 and MoE for 1..61.
    const MAX_LAYERS: usize = 128;
    if n_layers > MAX_LAYERS {
        let _ = writeln!(
            arch::serial::Serial,
            "[MoE] block_count > MAX_LAYERS — abort"
        );
        return;
    }
    let mut mlp_types: [zero_llm_inference::MlpType; MAX_LAYERS] =
        [zero_llm_inference::MlpType::Dense; MAX_LAYERS];
    for layer in 0..n_layers {
        let mut nb = [0u8; 64];
        let mut w = NameWriter {
            buf: &mut nb,
            pos: 0,
        };
        let _ = write!(w, "blk.{}.ffn_gate_exps.weight", layer);
        let pos = w.pos;
        let name = core::str::from_utf8(&nb[..pos]).unwrap_or("");
        if tensor_index.get(name).is_some() {
            mlp_types[layer] = zero_llm_inference::MlpType::MoE;
        }
    }

    // ── Per-layer AttnType dispatch (MLA vs standard MHA) ───────────
    //
    // Most DeepSeek-V2/V3 / Kimi K2.6 layers use MLA. Some builds ship
    // the first layer as standard Multi-Head Attention with the
    // `attn_q.weight` / `attn_k.weight` / `attn_v.weight` tensors instead
    // of the MLA tensors (`attn_kv_a_mqa.weight`, etc.). Detect by
    // presence of `attn_kv_a_mqa.weight` → MLA; otherwise → MHA. Also
    // count MHA layers and assign each one a compact `mha_layer_idx` so
    // the auxiliary MhaKvCache can be sized to just the MHA layer count.
    let mut attn_types: [zero_llm_inference::AttnType; MAX_LAYERS] =
        [zero_llm_inference::AttnType::Mla; MAX_LAYERS];
    let mut mha_layer_idx_map: [u32; MAX_LAYERS] = [0u32; MAX_LAYERS];
    let mut num_mha_layers: usize = 0;
    for layer in 0..n_layers {
        let mut nb = [0u8; 64];
        let mut w = NameWriter {
            buf: &mut nb,
            pos: 0,
        };
        let _ = write!(w, "blk.{}.attn_kv_a_mqa.weight", layer);
        let pos = w.pos;
        let name = core::str::from_utf8(&nb[..pos]).unwrap_or("");
        if tensor_index.get(name).is_none() {
            attn_types[layer] = zero_llm_inference::AttnType::Mha;
            mha_layer_idx_map[layer] = num_mha_layers as u32;
            num_mha_layers += 1;
        }
    }
    let _ = write!(arch::serial::Serial, "[MoE] num_mha_layers=");
    let _ = writeln!(arch::serial::Serial, "{}", num_mha_layers);

    let dummy_f32: &[f32] = &[];
    let dummy_u8: &[u8] = &[];
    // Default quants: harmless placeholders for the dummy entries that
    // never get used (layers > n_layers). Per-layer entries below
    // overwrite these with the real GGUF tensor_type for each weight.
    let dummy_quant = zero_gguf_parser::GgmlType::Q4K;
    let mut layers: [zero_llm_inference::Deepseek2LayerWeights<'_>; MAX_LAYERS] =
        core::array::from_fn(|_| zero_llm_inference::Deepseek2LayerWeights {
            mlp_type: zero_llm_inference::MlpType::Dense,
            attn_type: zero_llm_inference::AttnType::Mla,
            mha_layer_idx: 0,
            attn_norm: dummy_f32,
            attn_kv_a_mqa: dummy_u8,
            attn_kv_a_norm: dummy_f32,
            attn_kv_b: dummy_u8,
            attn_k_b: dummy_u8,
            attn_v_b: dummy_u8,
            attn_q_a: dummy_u8,
            attn_q_a_norm: dummy_f32,
            attn_q_b: dummy_u8,
            attn_output: dummy_u8,
            attn_k_norm: dummy_f32,
            attn_q_mha: dummy_u8,
            attn_k_mha: dummy_u8,
            attn_v_mha: dummy_u8,
            ffn_norm: dummy_f32,
            ffn_gate: dummy_u8,
            ffn_up: dummy_u8,
            ffn_down: dummy_u8,
            ffn_gate_inp: dummy_f32,
            ffn_gate_inp_bias: dummy_f32,
            ffn_gate_exps: dummy_u8,
            ffn_up_exps: dummy_u8,
            ffn_down_exps: dummy_u8,
            ffn_gate_shexp: dummy_u8,
            ffn_up_shexp: dummy_u8,
            ffn_down_shexp: dummy_u8,
            attn_quant: dummy_quant,
            expert_quant: dummy_quant,
            ffn_dense_quant: dummy_quant,
        });

    let _ = writeln!(arch::serial::Serial, "[DBG] layer load start");
    for layer in 0..n_layers {
        // Breadcrumb every 10 layers so serial shows progress even on slow UART.
        if layer % 10 == 0 {
            let _ = write!(arch::serial::Serial, "[MoE] loading layer ");
            let _ = write!(arch::serial::Serial, "{}", layer);
            let _ = write!(arch::serial::Serial, "/");
            let _ = writeln!(arch::serial::Serial, "{}", n_layers);
        }
        // Extra breadcrumb for layer 0 — first to fail if GGUF is malformed.
        if layer == 0 {
            let _ = writeln!(arch::serial::Serial, "[DBG] layer 0 loading...");
        }
        let attn_norm = lookup_f32(
            model_bytes,
            tensor_index,
            layer,
            "attn_norm.weight",
            embedding_dim,
        );
        let attn_output = lookup_bytes(model_bytes, tensor_index, layer, "attn_output.weight");
        let ffn_norm = lookup_f32(
            model_bytes,
            tensor_index,
            layer,
            "ffn_norm.weight",
            embedding_dim,
        );

        let attn_type = attn_types[layer];

        // ── Attention tensor lookups per attn_type ──────────────────
        //
        // MLA: 7 LoRA-style tensors + optional split/combined kv_b.
        // MHA: 3 direct projections (attn_q, attn_k, attn_v).
        // Both populate the "other" group with dummy slices so the
        // unused fields stay well-defined.
        let (
            attn_kv_a_mqa,
            attn_kv_a_norm,
            attn_kv_b,
            attn_k_b,
            attn_v_b,
            attn_q_a,
            attn_q_a_norm,
            attn_q_b,
            attn_k_norm,
            attn_q_mha,
            attn_k_mha,
            attn_v_mha,
        ) = if matches!(attn_type, zero_llm_inference::AttnType::Mla) {
            let mqa = lookup_bytes(model_bytes, tensor_index, layer, "attn_kv_a_mqa.weight");
            let mqa_norm = lookup_f32(
                model_bytes,
                tensor_index,
                layer,
                "attn_kv_a_norm.weight",
                kv_lora_rank,
            );
            // Kimi K2.6 ships attn_kv_b as two separate tensors
            // (attn_k_b + attn_v_b). Try the combined tensor first;
            // if it's missing AND both split tensors are present, feed
            // them through the split-format path in
            // mla_attention_single_token. Either form satisfies the layer.
            let kvb_combined = lookup_bytes(model_bytes, tensor_index, layer, "attn_kv_b.weight");
            let kb_split = lookup_bytes(model_bytes, tensor_index, layer, "attn_k_b.weight");
            let vb_split = lookup_bytes(model_bytes, tensor_index, layer, "attn_v_b.weight");
            let (kvb, kb, vb): (Option<&[u8]>, &[u8], &[u8]) =
                match (kvb_combined, kb_split, vb_split) {
                    (Some(c), _, _) => (Some(c), dummy_u8, dummy_u8),
                    (None, Some(k), Some(v)) => (Some(dummy_u8), k, v),
                    _ => (None, dummy_u8, dummy_u8),
                };
            let qa = lookup_bytes(model_bytes, tensor_index, layer, "attn_q_a.weight");
            let qan = lookup_f32(
                model_bytes,
                tensor_index,
                layer,
                "attn_q_a_norm.weight",
                q_lora_rank,
            );
            let qb = lookup_bytes(model_bytes, tensor_index, layer, "attn_q_b.weight");
            let kn = lookup_f32(
                model_bytes,
                tensor_index,
                layer,
                "attn_k_norm.weight",
                qk_rope_head_dim,
            )
            .unwrap_or(dummy_f32);
            (
                mqa,
                mqa_norm,
                kvb,
                kb,
                vb,
                qa,
                qan,
                qb,
                kn,
                Some(dummy_u8),
                Some(dummy_u8),
                Some(dummy_u8),
            )
        } else {
            // MHA layer — needs only attn_q / attn_k / attn_v.
            let q = lookup_bytes(model_bytes, tensor_index, layer, "attn_q.weight");
            let k = lookup_bytes(model_bytes, tensor_index, layer, "attn_k.weight");
            let v = lookup_bytes(model_bytes, tensor_index, layer, "attn_v.weight");
            (
                Some(dummy_u8),
                Some(dummy_f32),
                Some(dummy_u8),
                dummy_u8,
                dummy_u8,
                Some(dummy_u8),
                Some(dummy_f32),
                Some(dummy_u8),
                dummy_f32,
                q,
                k,
                v,
            )
        };

        let mlp_type = mlp_types[layer];
        let (ffn_gate, ffn_up, ffn_down) =
            if matches!(mlp_type, zero_llm_inference::MlpType::Dense) {
                (
                    lookup_bytes(model_bytes, tensor_index, layer, "ffn_gate.weight"),
                    lookup_bytes(model_bytes, tensor_index, layer, "ffn_up.weight"),
                    lookup_bytes(model_bytes, tensor_index, layer, "ffn_down.weight"),
                )
            } else {
                (Some(dummy_u8), Some(dummy_u8), Some(dummy_u8))
            };
        let (
            ffn_gate_inp,
            ffn_gate_exps,
            ffn_up_exps,
            ffn_down_exps,
            ffn_gate_shexp,
            ffn_up_shexp,
            ffn_down_shexp,
        ) = if matches!(mlp_type, zero_llm_inference::MlpType::MoE) {
            (
                lookup_f32(
                    model_bytes,
                    tensor_index,
                    layer,
                    "ffn_gate_inp.weight",
                    n_experts * embedding_dim,
                ),
                lookup_bytes(model_bytes, tensor_index, layer, "ffn_gate_exps.weight"),
                lookup_bytes(model_bytes, tensor_index, layer, "ffn_up_exps.weight"),
                lookup_bytes(model_bytes, tensor_index, layer, "ffn_down_exps.weight"),
                lookup_bytes(model_bytes, tensor_index, layer, "ffn_gate_shexp.weight"),
                lookup_bytes(model_bytes, tensor_index, layer, "ffn_up_shexp.weight"),
                lookup_bytes(model_bytes, tensor_index, layer, "ffn_down_shexp.weight"),
            )
        } else {
            (
                Some(dummy_f32),
                Some(dummy_u8),
                Some(dummy_u8),
                Some(dummy_u8),
                Some(dummy_u8),
                Some(dummy_u8),
                Some(dummy_u8),
            )
        };

        // SPEC: Kimi K2.6 / DeepSeek-V3 ship per-expert router bias
        // (shape [n_experts]) for load balancing. SilicatePack names it
        // `ffn_gate_inp_bias.weight`; llama.cpp GGUFs name the same
        // tensor `exp_probs_b.weight` — try both so the bias is not
        // silently dropped on the GGUF compatibility path. The bias is
        // optional — when absent under either name, feed an empty slice
        // and `moe_route_f32` falls back to a pure (router @ hidden)
        // score.
        let ffn_gate_inp_bias: &[f32] = if matches!(mlp_type, zero_llm_inference::MlpType::MoE) {
            lookup_f32(
                model_bytes,
                tensor_index,
                layer,
                "ffn_gate_inp_bias.weight",
                n_experts,
            )
            .or_else(|| {
                lookup_f32(
                    model_bytes,
                    tensor_index,
                    layer,
                    "exp_probs_b.weight",
                    n_experts,
                )
            })
            .unwrap_or(dummy_f32)
        } else {
            dummy_f32
        };

        if let (
            Some(an),
            Some(kvm),
            Some(kvn),
            Some(kvb),
            Some(qa),
            Some(qan),
            Some(qb),
            Some(ao),
            Some(fn_),
            Some(fg),
            Some(fu),
            Some(fd),
            Some(fgi),
            Some(fge),
            Some(fue),
            Some(fde),
            Some(fgs),
            Some(fus),
            Some(fds),
            Some(qmha),
            Some(kmha),
            Some(vmha),
        ) = (
            attn_norm,
            attn_kv_a_mqa,
            attn_kv_a_norm,
            attn_kv_b,
            attn_q_a,
            attn_q_a_norm,
            attn_q_b,
            attn_output,
            ffn_norm,
            ffn_gate,
            ffn_up,
            ffn_down,
            ffn_gate_inp,
            ffn_gate_exps,
            ffn_up_exps,
            ffn_down_exps,
            ffn_gate_shexp,
            ffn_up_shexp,
            ffn_down_shexp,
            attn_q_mha,
            attn_k_mha,
            attn_v_mha,
        ) {
            // Per-tensor quant detection — read `tensor_type` off
            // TensorIndex for one representative tensor per group, then
            // assume the rest of the group shares it (the GGUF
            // convention for both Qwen3 and Kimi K2.6 / DeepSeek-V3).
            // For MHA layers the canonical attn-group tensor is
            // attn_q.weight (the MLA-canonical attn_kv_a_mqa.weight
            // doesn't exist on those layers).
            let attn_quant = match attn_type {
                zero_llm_inference::AttnType::Mla => {
                    lookup_tensor_type(tensor_index, layer, "attn_kv_a_mqa.weight")
                        .unwrap_or(dummy_quant)
                }
                zero_llm_inference::AttnType::Mha => {
                    lookup_tensor_type(tensor_index, layer, "attn_q.weight").unwrap_or(dummy_quant)
                }
            };
            let expert_quant = match mlp_type {
                zero_llm_inference::MlpType::MoE => {
                    lookup_tensor_type(tensor_index, layer, "ffn_gate_exps.weight")
                        .unwrap_or(dummy_quant)
                }
                _ => dummy_quant,
            };
            let ffn_dense_quant = match mlp_type {
                zero_llm_inference::MlpType::Dense => {
                    lookup_tensor_type(tensor_index, layer, "ffn_gate.weight")
                        .unwrap_or(dummy_quant)
                }
                _ => dummy_quant,
            };

            layers[layer] = zero_llm_inference::Deepseek2LayerWeights {
                mlp_type,
                attn_type,
                mha_layer_idx: mha_layer_idx_map[layer],
                attn_norm: an,
                attn_kv_a_mqa: kvm,
                attn_kv_a_norm: kvn,
                attn_kv_b: kvb,
                attn_k_b,
                attn_v_b,
                attn_q_a: qa,
                attn_q_a_norm: qan,
                attn_q_b: qb,
                attn_output: ao,
                attn_k_norm,
                attn_q_mha: qmha,
                attn_k_mha: kmha,
                attn_v_mha: vmha,
                ffn_norm: fn_,
                ffn_gate: fg,
                ffn_up: fu,
                ffn_down: fd,
                ffn_gate_inp: fgi,
                ffn_gate_inp_bias,
                ffn_gate_exps: fge,
                ffn_up_exps: fue,
                ffn_down_exps: fde,
                ffn_gate_shexp: fgs,
                ffn_up_shexp: fus,
                ffn_down_shexp: fds,
                attn_quant,
                expert_quant,
                ffn_dense_quant,
            };
        } else {
            let _ = write!(arch::serial::Serial, "[MoE] missing tensor at layer ");
            let _ = writeln!(arch::serial::Serial, "{}", layer);
            // Diagnose WHICH tensor is None so we can fix the exact gap.
            if attn_norm.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> attn_norm = None");
            }
            if attn_kv_a_mqa.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> attn_kv_a_mqa = None");
            }
            if attn_kv_a_norm.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> attn_kv_a_norm = None");
            }
            if attn_kv_b.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> attn_kv_b = None");
            }
            if attn_q_a.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> attn_q_a = None");
            }
            if attn_q_a_norm.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> attn_q_a_norm = None");
            }
            if attn_q_b.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> attn_q_b = None");
            }
            if attn_output.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> attn_output = None");
            }
            if ffn_norm.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> ffn_norm = None");
            }
            if ffn_gate.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> ffn_gate = None");
            }
            if ffn_up.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> ffn_up = None");
            }
            if ffn_down.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> ffn_down = None");
            }
            if ffn_gate_inp.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> ffn_gate_inp = None");
            }
            if ffn_gate_exps.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> ffn_gate_exps = None");
            }
            if ffn_up_exps.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> ffn_up_exps = None");
            }
            if ffn_down_exps.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> ffn_down_exps = None");
            }
            if ffn_gate_shexp.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> ffn_gate_shexp = None");
            }
            if ffn_up_shexp.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> ffn_up_shexp = None");
            }
            if ffn_down_shexp.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> ffn_down_shexp = None");
            }
            if attn_q_mha.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> attn_q_mha = None");
            }
            if attn_k_mha.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> attn_k_mha = None");
            }
            if attn_v_mha.is_none() {
                let _ = writeln!(arch::serial::Serial, "  -> attn_v_mha = None");
            }
            return;
        }
    }
    let _ = writeln!(arch::serial::Serial, "[DBG] all layers loaded");
    let _ = write!(arch::serial::Serial, "[MoE] layers loaded: ");
    let _ = writeln!(arch::serial::Serial, "{}", n_layers);

    let token_embd = match get_weight_bytes(model_bytes, tensor_index, "token_embd.weight") {
        Some(b) => b,
        None => {
            let _ = writeln!(arch::serial::Serial, "[MoE] token_embd.weight missing");
            return;
        }
    };
    // Quant of the token-embedding tensor — passed into the forward
    // pass so embed_lookup_dispatch picks the right dequantiser.
    let embed_quant = match tensor_index.get("token_embd.weight") {
        Some(t) => t.tensor_type,
        None => {
            let _ = writeln!(arch::serial::Serial, "[MoE] token_embd.weight missing");
            return;
        }
    };
    let output_norm_w = match get_f32_weight(
        model_bytes,
        tensor_index,
        "output_norm.weight",
        embedding_dim,
    ) {
        Some(b) => b,
        None => {
            let _ = writeln!(arch::serial::Serial, "[MoE] output_norm.weight missing");
            return;
        }
    };
    let output_w_tensor = match tensor_index.get("output.weight") {
        Some(t) => t,
        None => {
            let _ = writeln!(arch::serial::Serial, "[MoE] output.weight missing");
            return;
        }
    };
    let output_quant = match output_w_tensor.tensor_type {
        zero_gguf_parser::GgmlType::Q4K => zero_llm_inference::OutputQuant::Q4K,
        zero_gguf_parser::GgmlType::Q6K => zero_llm_inference::OutputQuant::Q6K,
        // Q8_0 keeps appearing as the output.weight quant in
        // bartowski-style Q4_0 builds — the LM head needs the higher
        // precision because the 4-bit format isn't accurate enough
        // for the per-token logit comparison.
        zero_gguf_parser::GgmlType::Q8_0 => zero_llm_inference::OutputQuant::Q8_0,
        zero_gguf_parser::GgmlType::Q4_0 => zero_llm_inference::OutputQuant::Q4_0,
        _ => {
            let _ = writeln!(
                arch::serial::Serial,
                "[MoE] output.weight quant unsupported"
            );
            return;
        }
    };
    let output_w = match get_weight_bytes(model_bytes, tensor_index, "output.weight") {
        Some(b) => b,
        None => {
            let _ = writeln!(arch::serial::Serial, "[MoE] output.weight bytes missing");
            return;
        }
    };

    let _ = writeln!(
        arch::serial::Serial,
        "[MoE] global weights loaded — allocating KV cache"
    );

    // ── KV-cache (MLA, compressed-latent) ───────────────────────────
    //
    // Per (layer, token) we cache only the low-rank latent
    // `c_kv_normed[kv_lora_rank]` plus the post-RoPE shared
    // `k_rope[qk_rope_head_dim]`. Per-head k_nope and v are re-derived
    // at attention time via on-the-fly W_kv_b expansion (see
    // `mla::mla_attention_single_token`). For Kimi K2.6 this is ~36×
    // less KV memory than the previous decompressed K+V layout.
    //
    // `max_tokens` is sized dynamically from the KV arena: we compute
    // how many tokens fit in `KV_CACHE_ARENA_SIZE` given the compressed
    // per-token footprint, then clamp to the 8K production ceiling.
    let per_token_f32 = kv_lora_rank + qk_rope_head_dim;
    let per_layer_token_bytes = per_token_f32 * 4;
    let total_per_token_bytes = per_layer_token_bytes * n_layers;
    // Reserve ~5% headroom for arena bookkeeping / alignment slop.
    let usable_arena = (memory::KV_CACHE_ARENA_SIZE / 20) * 19;
    let from_arena = if total_per_token_bytes > 0 {
        usable_arena / total_per_token_bytes
    } else {
        0
    };
    // Hard ceiling: don't context past 8 192 tokens regardless of
    // arena (production Kimi K2.6 ships with 8K context, and the
    // KvCache lookup gets slow well before then).
    let max_tokens: usize = from_arena.min(8192);
    if max_tokens < 4 {
        let _ = write!(
            arch::serial::Serial,
            "[MoE] KV arena too small for any usable context (per_token_bytes_total="
        );
        let _ = write!(arch::serial::Serial, "{}", total_per_token_bytes);
        let _ = write!(arch::serial::Serial, ", arena=");
        let _ = write!(arch::serial::Serial, "{}", memory::KV_CACHE_ARENA_SIZE);
        let _ = writeln!(arch::serial::Serial, " B)");
        return;
    }
    let _ = write!(arch::serial::Serial, "[MoE] max_tokens=");
    let _ = write!(arch::serial::Serial, "{}", max_tokens);
    let _ = write!(arch::serial::Serial, " (per_token_bytes_total=");
    let _ = write!(arch::serial::Serial, "{}", total_per_token_bytes);
    let _ = write!(arch::serial::Serial, ", arena=");
    let _ = write!(arch::serial::Serial, "{}", memory::KV_CACHE_ARENA_SIZE);
    let _ = writeln!(arch::serial::Serial, " B)");
    let mla_kv_f32 = zero_llm_inference::MlaKvCache::required_f32(
        max_tokens,
        n_layers,
        kv_lora_rank,
        qk_rope_head_dim,
    );
    let mut kv_guard = memory::KV_CACHE_ARENA.lock();
    let kv_storage = match kv_guard.as_mut() {
        Some(arena) => match arena.alloc_f32_slice(mla_kv_f32) {
            Ok(s) => s,
            Err(_) => {
                let _ = write!(
                    arch::serial::Serial,
                    "[MoE] KV_CACHE_ARENA too small, need_f32="
                );
                let _ = writeln!(arch::serial::Serial, "{}", mla_kv_f32);
                return;
            }
        },
        None => {
            let _ = writeln!(arch::serial::Serial, "[MoE] KV_CACHE_ARENA not initialized");
            return;
        }
    };
    let mut mla_cache = zero_llm_inference::MlaKvCache::new(
        kv_storage.as_mut_ptr(),
        max_tokens,
        n_layers,
        kv_lora_rank,
        qk_rope_head_dim,
    );
    drop(kv_guard);

    // ── Auxiliary MHA KV cache (for layers with attn_type == Mha) ───
    //
    // DeepSeek-V2 / Kimi K2.6 standard-MHA layers (typically layer 0
    // only) need a full per-token K/V cache. Sized to just the MHA
    // layer count so MLA-mostly builds don't pay the cost.
    let mha_head_dim_qk = qk_nope_head_dim + qk_rope_head_dim;
    let mha_kv_f32 = if num_mha_layers > 0 {
        zero_llm_inference::MhaKvCache::required_f32(
            max_tokens,
            num_mha_layers,
            n_kv_heads,
            mha_head_dim_qk,
            v_head_dim,
        )
    } else {
        0
    };
    let mut mha_cache_opt: Option<zero_llm_inference::MhaKvCache> = None;
    if num_mha_layers > 0 {
        let mut kv_guard = memory::KV_CACHE_ARENA.lock();
        let mha_storage = match kv_guard.as_mut() {
            Some(arena) => match arena.alloc_f32_slice(mha_kv_f32) {
                Ok(s) => s,
                Err(_) => {
                    let _ = write!(
                        arch::serial::Serial,
                        "[MoE] MHA KV cache alloc failed, need_f32="
                    );
                    let _ = writeln!(arch::serial::Serial, "{}", mha_kv_f32);
                    return;
                }
            },
            None => {
                let _ = writeln!(arch::serial::Serial, "[MoE] KV_CACHE_ARENA missing (MHA)");
                return;
            }
        };
        mha_cache_opt = Some(zero_llm_inference::MhaKvCache::new(
            mha_storage.as_mut_ptr(),
            max_tokens,
            num_mha_layers,
            n_kv_heads,
            mha_head_dim_qk,
            v_head_dim,
        ));
        drop(kv_guard);
        let _ = write!(arch::serial::Serial, "[MoE] MHA KV cache allocated, f32=");
        let _ = writeln!(arch::serial::Serial, "{}", mha_kv_f32);
    }

    let _ = writeln!(
        arch::serial::Serial,
        "[MoE] KV cache ready — sizing activation scratch"
    );

    // ── Scratch slot sizing (Pillar 1: hot-path zero alloc) ─────────
    let ffn_buf_dim = core::cmp::max(feed_forward_length, expert_intermediate);
    let kv_a_out_dim = kv_lora_rank + qk_rope_head_dim;
    let mla_kv_b_dim = n_heads * (qk_nope_head_dim + v_head_dim);
    // For MHA layers, kv_decompressed is split as
    //   [K: n_kv_heads × head_dim_qk | V: n_kv_heads × v_head_dim]
    // so it must hold at least n_kv_heads × (head_dim_qk + v_head_dim).
    let mha_kv_b_dim = if num_mha_layers > 0 {
        n_kv_heads * (mha_head_dim_qk + v_head_dim)
    } else {
        0
    };
    let kv_b_out_dim = core::cmp::max(mla_kv_b_dim, mha_kv_b_dim);
    let q_b_out_dim = n_heads * (qk_nope_head_dim + qk_rope_head_dim);
    let total_k_head_dim = qk_nope_head_dim + qk_rope_head_dim;

    let total_f32 = embedding_dim                       // hidden
        + embedding_dim                                  // norm_buf
        + embedding_dim                                  // attn_out
        + embedding_dim                                  // mlp_out
        + kv_a_out_dim                                   // c_kv_rope_buf
        + kv_lora_rank                                   // c_kv_norm_buf
        + kv_b_out_dim                                   // kv_decompressed
        + q_lora_rank                                    // c_q_buf
        + q_lora_rank                                    // c_q_norm_buf
        + q_b_out_dim                                    // q_decompressed
        + n_heads * total_k_head_dim                     // k_assembled (unused under compressed cache)
        + n_heads * max_tokens                           // score_buf (per-head stripe)
        + v_head_dim                                     // attn_head_buf (unused under compressed cache)
        + n_heads * v_head_dim                           // attn_concat
        + ffn_buf_dim                                    // gate_buf
        + ffn_buf_dim                                    // up_buf
        + ffn_buf_dim                                    // hidden_mlp_buf
        + n_experts                                      // router_score_buf
        + top_k                                          // expert_weights
        + embedding_dim                                  // expert_out_buf
        + vocab_size_padded; // logits_buf

    let mut act_guard = memory::ACTIVATION_ARENA.lock();
    let act_arena = match act_guard.as_mut() {
        Some(a) => a,
        None => {
            let _ = writeln!(
                arch::serial::Serial,
                "[MoE] ACTIVATION_ARENA not initialized"
            );
            return;
        }
    };
    let buf = match act_arena.alloc_f32_slice(total_f32) {
        Ok(s) => s,
        Err(_) => {
            let _ = write!(
                arch::serial::Serial,
                "[MoE] ACTIVATION_ARENA too small, need_f32="
            );
            let _ = writeln!(arch::serial::Serial, "{}", total_f32);
            return;
        }
    };
    let expert_indices_buf = match act_arena.alloc_u32_slice(top_k) {
        Ok(s) => s,
        Err(_) => {
            let _ = writeln!(arch::serial::Serial, "[MoE] u32 indices alloc failed");
            return;
        }
    };
    drop(act_guard);

    let (hidden, rest) = buf.split_at_mut(embedding_dim);
    let (norm_buf, rest) = rest.split_at_mut(embedding_dim);
    let (attn_out, rest) = rest.split_at_mut(embedding_dim);
    let (mlp_out, rest) = rest.split_at_mut(embedding_dim);
    let (c_kv_rope_buf, rest) = rest.split_at_mut(kv_a_out_dim);
    let (c_kv_norm_buf, rest) = rest.split_at_mut(kv_lora_rank);
    let (kv_decompressed, rest) = rest.split_at_mut(kv_b_out_dim);
    let (c_q_buf, rest) = rest.split_at_mut(q_lora_rank);
    let (c_q_norm_buf, rest) = rest.split_at_mut(q_lora_rank);
    let (q_decompressed, rest) = rest.split_at_mut(q_b_out_dim);
    let (k_assembled, rest) = rest.split_at_mut(n_heads * total_k_head_dim);
    let (score_buf, rest) = rest.split_at_mut(n_heads * max_tokens);
    let (attn_head_buf, rest) = rest.split_at_mut(v_head_dim);
    let (attn_concat, rest) = rest.split_at_mut(n_heads * v_head_dim);
    let (gate_buf, rest) = rest.split_at_mut(ffn_buf_dim);
    let (up_buf, rest) = rest.split_at_mut(ffn_buf_dim);
    let (hidden_mlp_buf, rest) = rest.split_at_mut(ffn_buf_dim);
    let (router_score_buf, rest) = rest.split_at_mut(n_experts);
    let (expert_weights, rest) = rest.split_at_mut(top_k);
    let (expert_out_buf, rest) = rest.split_at_mut(embedding_dim);
    let logits_buf = &mut rest[..vocab_size_padded];

    let _ = writeln!(
        arch::serial::Serial,
        "[MoE] activation arena allocated — building scratch struct"
    );

    let mut lin_scratch = zero_llm_inference::LinearScratch::new();
    let mut scratch = zero_llm_inference::Deepseek2Scratch {
        hidden,
        norm_buf,
        attn_out,
        mlp_out,
        c_kv_rope_buf,
        c_kv_norm_buf,
        kv_decompressed,
        c_q_buf,
        c_q_norm_buf,
        q_decompressed,
        k_assembled,
        score_buf,
        attn_head_buf,
        attn_concat,
        gate_buf,
        up_buf,
        hidden_mlp_buf,
        router_score_buf,
        expert_indices: expert_indices_buf,
        expert_weights,
        expert_out_buf,
        scratch: &mut lin_scratch,
    };

    // ── Runtime vocab install ────────────────────────────────────────
    //
    // `parse_selective` does NOT materialise `tokenizer.ggml.tokens`
    // because it would OOM the runtime arena for any non-trivial vocab.
    // The deepseek2 path explicitly re-walks the GGUF here via
    // `extract_tokenizer_from_bytes` — single pass, two flat
    // allocations (bytes + offsets), then handed to the detokenizer.
    // Qwen3 never reaches this code (different architecture, different
    // `run_forward_pass` branch) so the β-anchor stays bit-exact.
    let runtime_tokenizer = match zero_gguf_parser::extract_tokenizer_from_bytes(model_bytes) {
        Ok(t) => t,
        Err(e) => {
            let _ = writeln!(
                arch::serial::Serial,
                "[MoE] tokenizer extraction failed: {:?} — falling back to compile-time vocab",
                e
            );
            None
        }
    };
    if let Some(ref tokenizer) = runtime_tokenizer {
        match detokenizer::install_runtime_vocab(tokenizer) {
            Ok(()) => {}
            Err(()) => {
                let _ = writeln!(
                    arch::serial::Serial,
                    "[MoE] runtime vocab install FAILED — falling back to compile-time vocab"
                );
            }
        }
    } else {
        let _ = writeln!(
            arch::serial::Serial,
            "[MoE] GGUF has no tokenizer.ggml.tokens — using compile-time vocab"
        );
    }

    // ── EOS / BOS resolution ─────────────────────────────────────────
    //
    // Read from GGUF metadata (`tokenizer.ggml.eos_token_id` /
    // `bos_token_id`). NO architecture-specific fallback IDs — those
    // were always a guess and got Llama-3-vs-Kimi wrong. The only
    // fallback is `vocab_size - 1` as a hard-stop sentinel for
    // malformed GGUFs.
    let tok_eos = runtime_tokenizer
        .as_ref()
        .and_then(|t| t.eos_token_id)
        .or(cfg.eos_token_id);
    let tok_bos = runtime_tokenizer.as_ref().and_then(|t| t.bos_token_id);
    let last_resort_eos = (vocab_size_padded as u32).saturating_sub(1);
    let eos_primary = tok_eos.unwrap_or(last_resort_eos);
    // No secondary fallback — the GGUF only ships one canonical EOS.
    // Some Kimi K2.6 builds add `<|im_end|>` at id ≠ eos but that's
    // model-specific; the operator can post-hoc patch this constant
    // if a deployment needs multi-EOS.
    let eos_secondary: Option<u32> = None;
    let _ = writeln!(
        arch::serial::Serial,
        "[MoE] EOS: primary={} (from_gguf={}); BOS: {} (from_gguf={})",
        eos_primary,
        tok_eos.is_some(),
        match tok_bos {
            Some(b) => b as i64,
            None => -1,
        },
        tok_bos.is_some(),
    );

    // ── Phase 1 — prompt prefill ─────────────────────────────────────
    //
    // If `DEEPSEEK2_PROMPT_TOKENS` has been populated by
    // `tools/encode_prompt_kimi.py`, feed those tokens verbatim —
    // they encode the Zero system message under Kimi's ChatML
    // template. Otherwise fall back to the GGUF's BOS marker so the
    // boot path still completes a single-token prefill for HW bring-
    // up. `prefill_seed` is bound in both branches so existing
    // diagnostic prints keep their meaning.
    let bos_seed: u32 = tok_bos.unwrap_or(0);
    let prefill_tokens: &[u32] = if !DEEPSEEK2_PROMPT_TOKENS.is_empty() {
        DEEPSEEK2_PROMPT_TOKENS
    } else {
        core::slice::from_ref(&bos_seed)
    };
    let prefill_seed: u32 = prefill_tokens[0];
    let prefill_count: usize = prefill_tokens.len();
    if prefill_count >= max_tokens {
        let _ = writeln!(
            arch::serial::Serial,
            "[MoE] prefill_count >= max_tokens — KV arena too small"
        );
        return;
    }
    if prefill_count > DEEPSEEK2_MAX_PREFILL {
        let _ = writeln!(
            arch::serial::Serial,
            "[MoE] DEEPSEEK2_PROMPT_TOKEN_COUNT exceeds DEEPSEEK2_MAX_PREFILL — refusing prefill"
        );
        return;
    }

    let _ = writeln!(arch::serial::Serial, "[DBG] prefill start");
    let _ = write!(arch::serial::Serial, "[MoE] Phase 1 prefill: seed=");
    let _ = write!(arch::serial::Serial, "{}", prefill_seed);
    let _ = write!(arch::serial::Serial, " (");
    let _ = write!(arch::serial::Serial, "{}", prefill_count);
    if DEEPSEEK2_PROMPT_TOKENS.is_empty() {
        let _ = writeln!(arch::serial::Serial, " token, source=bos)");
    } else {
        let _ = writeln!(
            arch::serial::Serial,
            " tokens, source=DEEPSEEK2_PROMPT_TOKENS)"
        );
    }

    // Sequence buffer holds prompt + generated tokens. Bound at
    // compile time so the loop is no-alloc; size lines up with
    // `max_tokens` ceiling from the KV cache.
    // Sequence buffer holds prefill + generated tokens on the stack.
    // Sized to comfortably fit `DEEPSEEK2_MAX_PREFILL` (32) prefix
    // tokens plus `DEEPSEEK2_MAX_GENERATED_TOKENS` (512) plus
    // bookkeeping headroom. 1024 × 4 B = 4 KiB on the boot stack.
    const MAX_SEQUENCE: usize = 1024;
    let mut sequence: [u32; MAX_SEQUENCE] = [0; MAX_SEQUENCE];
    let mut sequence_len: usize = 0;

    let prefill_wall_t0 = arch::read_cycles();
    let mut prefill_forward_cycles: u64 = 0;

    let mut phase1_ok = true;
    for i in 0..prefill_count {
        if i == 0 {
            let _ = writeln!(arch::serial::Serial, "[DBG] token 0 forward starting");
        }
        let tok = prefill_tokens[i];
        sequence[sequence_len] = tok;
        let position = sequence_len;
        sequence_len += 1;

        let fwd_t0 = arch::read_cycles();
        #[cfg(not(any(
            all(target_arch = "x86_64", feature = "avx512-acceleration"),
            all(target_arch = "aarch64", feature = "neon-acceleration"),
        )))]
        let fwd_res = zero_llm_inference::forward_single_token_deepseek2(
            tok,
            position,
            &layers[..n_layers],
            token_embd,
            embed_quant,
            rms_eps,
            rope_freq_base,
            embedding_dim,
            feed_forward_length,
            n_heads,
            n_kv_heads,
            kv_lora_rank,
            q_lora_rank,
            qk_nope_head_dim,
            qk_rope_head_dim,
            v_head_dim,
            n_experts,
            top_k,
            expert_intermediate,
            routing_mode,
            expert_weight_scale,
            &mut mla_cache,
            mha_cache_opt.as_mut(),
            &mut scratch,
        )
        .map(|_| ())
        .map_err(|_| ());

        #[cfg(all(target_arch = "x86_64", feature = "avx512-acceleration"))]
        let fwd_res = unsafe {
            crate::inference_avx512::forward_single_token_deepseek2_avx512(
                tok,
                position,
                &layers[..n_layers],
                token_embd,
                embed_quant,
                rms_eps,
                rope_freq_base,
                embedding_dim,
                feed_forward_length,
                n_heads,
                n_kv_heads,
                n_experts,
                top_k,
                expert_intermediate,
                kv_lora_rank,
                qk_nope_head_dim,
                qk_rope_head_dim,
                v_head_dim,
                q_lora_rank,
                routing_mode,
                expert_weight_scale,
                &mut mla_cache,
                mha_cache_opt.as_mut(),
                &mut scratch,
            )
        }
        .map(|_| ())
        .map_err(|_| ());

        #[cfg(all(target_arch = "aarch64", feature = "neon-acceleration"))]
        let fwd_res = unsafe {
            crate::inference_neon::forward_single_token_deepseek2_neon(
                tok,
                position,
                &layers[..n_layers],
                token_embd,
                embed_quant,
                rms_eps,
                rope_freq_base,
                embedding_dim,
                feed_forward_length,
                n_heads,
                n_kv_heads,
                n_experts,
                top_k,
                expert_intermediate,
                kv_lora_rank,
                qk_nope_head_dim,
                qk_rope_head_dim,
                v_head_dim,
                q_lora_rank,
                routing_mode,
                expert_weight_scale,
                &mut mla_cache,
                mha_cache_opt.as_mut(),
                &mut scratch,
            )
        }
        .map(|_| ())
        .map_err(|_| ());

        prefill_forward_cycles =
            prefill_forward_cycles.wrapping_add(arch::read_cycles().wrapping_sub(fwd_t0));

        if fwd_res.is_err() {
            let _ = write!(
                arch::serial::Serial,
                "[MoE] prefill forward FAILED at token "
            );
            let _ = writeln!(arch::serial::Serial, "{}", i);
            phase1_ok = false;
            break;
        }
        if i == 0 {
            let _ = writeln!(arch::serial::Serial, "[DBG] token 0 forward done");
        }
    }

    if !phase1_ok {
        let _ = writeln!(arch::serial::Serial, "[MoE] Phase 1 aborted");
        return;
    }
    let prefill_wall_cycles = arch::read_cycles().wrapping_sub(prefill_wall_t0);
    let _ = write!(arch::serial::Serial, "[MoE] Phase 1 complete (");
    let _ = write!(arch::serial::Serial, "{}", prefill_count);
    let _ = write!(arch::serial::Serial, " tokens, forward_cycles=");
    let _ = write!(arch::serial::Serial, "{}", prefill_forward_cycles);
    let _ = write!(arch::serial::Serial, ", wall_cycles=");
    let _ = writeln!(arch::serial::Serial, "{}", prefill_wall_cycles);

    // ── First generated token (LM-head over last prefill position) ───
    let lm_t0 = arch::read_cycles();
    let lm0 = zero_llm_inference::lm_head_argmax_dynamic(
        scratch.hidden,
        output_norm_w,
        output_w,
        output_quant,
        rms_eps,
        embedding_dim,
        vocab_size_padded,
        vocab_size_real,
        scratch.norm_buf,
        logits_buf,
        scratch.scratch,
    );
    let lm0_cycles = arch::read_cycles().wrapping_sub(lm_t0);
    let first_gen_token = match lm0 {
        Ok(t) => t,
        Err(_) => {
            // Why: Phase 1's final LM head pick failed — the deepseek2
            // generation loop can't even start. Surface a structured
            // [BENCH] failure entry so the summary screen reports
            // "FAILED" instead of "unavailable" and the operator sees
            // the tensor identity that needs investigation.
            let _ = writeln!(
                arch::serial::Serial,
                "[MoE] LM head failed (Phase 1 last token) embedding_dim={} vocab_size_padded={} vocab_size_real={} (tensor: output_norm.weight / output.weight)",
                embedding_dim, vocab_size_padded, vocab_size_real,
            );
            #[cfg(target_arch = "x86_64")]
            crate::bench::record_llm_failure(
                "Phase 1 lm_head_argmax_dynamic Err (output_norm / output.weight)",
            );
            return;
        }
    };

    if smodel_validation_anchor.is_some() || native_smodel_profile {
        let mut top1_bits: u32 = 0;
        let mut top1_logit = f32::NEG_INFINITY;
        for &logit in logits_buf.iter().take(vocab_size_real) {
            if logit > top1_logit {
                top1_logit = logit;
                top1_bits = logit.to_bits();
            }
        }

        if let Some(anchor) = smodel_validation_anchor {
            if !anchor.matched_runtime {
                let _ = writeln!(
                    arch::serial::Serial,
                    "[MoE] .smodel ANCHOR FAIL: no matching {}/{} validation anchor — HALTING",
                    crate::model_loader::smodel_expected_anchor_target_arch(),
                    crate::model_loader::smodel_expected_anchor_profile()
                );
                if anchor.strict {
                    #[cfg(target_arch = "x86_64")]
                    crate::bench::record_llm_failure("DeepSeek2 .smodel anchor profile mismatch");
                    loop {
                        core::hint::spin_loop();
                    }
                }
            }
            let _ = writeln!(arch::serial::Serial, "[MoE] .smodel anchor verification:");
            let _ = write!(arch::serial::Serial, "[MoE]   token = {}", first_gen_token);
            if let Some(expected) = anchor.expected_next_token {
                let _ = write!(arch::serial::Serial, " (expected {})", expected);
            }
            let _ = writeln!(arch::serial::Serial);
            let _ = write!(
                arch::serial::Serial,
                "[MoE]   logit_bits = 0x{:08x}",
                top1_bits
            );
            if let Some(expected) = anchor.expected_logit_bits {
                let _ = write!(arch::serial::Serial, " (expected 0x{:08x})", expected);
            }
            let _ = writeln!(arch::serial::Serial);

            if anchor.strict {
                if anchor.expected_next_token != Some(first_gen_token) {
                    let _ = writeln!(
                        arch::serial::Serial,
                        "[MoE] .smodel ANCHOR FAIL: Token-ID mismatch — HALTING"
                    );
                    #[cfg(target_arch = "x86_64")]
                    crate::bench::record_llm_failure("DeepSeek2 .smodel anchor token mismatch");
                    loop {
                        core::hint::spin_loop();
                    }
                }
                if anchor.expected_logit_bits != Some(top1_bits) {
                    let _ = writeln!(
                        arch::serial::Serial,
                        "[MoE] .smodel ANCHOR FAIL: logit_bits mismatch — HALTING"
                    );
                    #[cfg(target_arch = "x86_64")]
                    crate::bench::record_llm_failure(
                        "DeepSeek2 .smodel anchor logit_bits mismatch",
                    );
                    loop {
                        core::hint::spin_loop();
                    }
                }
                let _ = writeln!(
                    arch::serial::Serial,
                    "[MoE] .smodel anchor PASS: manifest chain preserved"
                );
            } else {
                let _ = writeln!(
                    arch::serial::Serial,
                    "[MoE] .smodel anchor captured: token={} logit_bits=0x{:08x}; manifest mode=capture",
                    first_gen_token,
                    top1_bits
                );
            }
        } else {
            let _ = writeln!(
                arch::serial::Serial,
                "[MoE] native .smodel anchor missing: token={} logit_bits=0x{:08x}; strict validation not available",
                first_gen_token,
                top1_bits
            );
        }
    }

    let _ = write!(arch::serial::Serial, "[MoE] gen[0] = ");
    let _ = write!(arch::serial::Serial, "{}", first_gen_token);
    let _ = write!(arch::serial::Serial, " (lm_head_cycles=");
    let _ = write!(arch::serial::Serial, "{}", lm0_cycles);
    let _ = writeln!(arch::serial::Serial, ")");

    sequence[sequence_len] = first_gen_token;
    sequence_len += 1;

    // Detokenizer render (no-op when kimi-k26-vocab stub is in use).
    let decoded0 = detokenizer::decode_token(first_gen_token);
    if !decoded0.is_empty() {
        let _ = arch::serial::Serial.write_str("[MoE] decoded: ");
        let _ =
            arch::serial::Serial.write_str(core::str::from_utf8(decoded0).unwrap_or("<non-utf8>"));
        let _ = arch::serial::Serial.write_str("\n");
    }

    let eos_matches =
        |t: u32| -> bool { t == eos_primary || matches!(eos_secondary, Some(s) if s == t) };

    let mut generated_count: usize = 1;
    let mut eos_stopped = eos_matches(first_gen_token);
    if eos_stopped {
        let _ = writeln!(arch::serial::Serial, "[MoE] EOS at gen[0], stopping");
    }

    // ── Phase 2 — autoregressive generation ──────────────────────────
    let _ = writeln!(arch::serial::Serial, "[DBG] generation start");
    let gen_wall_t0 = arch::read_cycles();
    let mut gen_forward_cycles: u64 = 0;
    let mut gen_lm_cycles: u64 = 0;
    // Why: route post-loop diagnostics through `record_llm_failure` so
    // the bench summary distinguishes "aborted forward pass" from
    // "Stage 11 never ran" instead of collapsing both to "unavailable".
    let mut gen_abort_reason: Option<&'static str> = None;

    while !eos_stopped && generated_count < DEEPSEEK2_MAX_GENERATED_TOKENS {
        if sequence_len >= max_tokens || sequence_len >= MAX_SEQUENCE {
            // Why: KV-cache (`max_tokens`) or stack sequence buffer
            // (`MAX_SEQUENCE`) is full — autoregressive generation must
            // stop. Print which ceiling fired so the operator can grow
            // the right one.
            let _ = writeln!(
                arch::serial::Serial,
                "[MoE] sequence_len ceiling hit: sequence_len={} max_tokens={} MAX_SEQUENCE={} (limit={}) gen_so_far={} prefill={}",
                sequence_len,
                max_tokens,
                MAX_SEQUENCE,
                if sequence_len >= max_tokens { "kv_cache(max_tokens)" } else { "stack(MAX_SEQUENCE)" },
                generated_count,
                prefill_count,
            );
            gen_abort_reason = Some("sequence_len ceiling (KV-cache or stack full)");
            break;
        }
        let current_token = sequence[sequence_len - 1];
        let position = sequence_len - 1;

        let fwd_t0 = arch::read_cycles();
        #[cfg(not(any(
            all(target_arch = "x86_64", feature = "avx512-acceleration"),
            all(target_arch = "aarch64", feature = "neon-acceleration"),
        )))]
        let fwd_res = zero_llm_inference::forward_single_token_deepseek2(
            current_token,
            position,
            &layers[..n_layers],
            token_embd,
            embed_quant,
            rms_eps,
            rope_freq_base,
            embedding_dim,
            feed_forward_length,
            n_heads,
            n_kv_heads,
            kv_lora_rank,
            q_lora_rank,
            qk_nope_head_dim,
            qk_rope_head_dim,
            v_head_dim,
            n_experts,
            top_k,
            expert_intermediate,
            routing_mode,
            expert_weight_scale,
            &mut mla_cache,
            mha_cache_opt.as_mut(),
            &mut scratch,
        )
        .map(|_| ())
        .map_err(|_| ());

        #[cfg(all(target_arch = "x86_64", feature = "avx512-acceleration"))]
        let fwd_res = unsafe {
            crate::inference_avx512::forward_single_token_deepseek2_avx512(
                current_token,
                position,
                &layers[..n_layers],
                token_embd,
                embed_quant,
                rms_eps,
                rope_freq_base,
                embedding_dim,
                feed_forward_length,
                n_heads,
                n_kv_heads,
                n_experts,
                top_k,
                expert_intermediate,
                kv_lora_rank,
                qk_nope_head_dim,
                qk_rope_head_dim,
                v_head_dim,
                q_lora_rank,
                routing_mode,
                expert_weight_scale,
                &mut mla_cache,
                mha_cache_opt.as_mut(),
                &mut scratch,
            )
        }
        .map(|_| ())
        .map_err(|_| ());

        #[cfg(all(target_arch = "aarch64", feature = "neon-acceleration"))]
        let fwd_res = unsafe {
            crate::inference_neon::forward_single_token_deepseek2_neon(
                current_token,
                position,
                &layers[..n_layers],
                token_embd,
                embed_quant,
                rms_eps,
                rope_freq_base,
                embedding_dim,
                feed_forward_length,
                n_heads,
                n_kv_heads,
                n_experts,
                top_k,
                expert_intermediate,
                kv_lora_rank,
                qk_nope_head_dim,
                qk_rope_head_dim,
                v_head_dim,
                q_lora_rank,
                routing_mode,
                expert_weight_scale,
                &mut mla_cache,
                mha_cache_opt.as_mut(),
                &mut scratch,
            )
        }
        .map(|_| ())
        .map_err(|_| ());

        gen_forward_cycles =
            gen_forward_cycles.wrapping_add(arch::read_cycles().wrapping_sub(fwd_t0));
        if fwd_res.is_err() {
            // Why: deepseek2 forward returned an MLA/quant error.
            // The lib-side error is mapped to `()` at the kernel
            // boundary; the diagnostic here at least nails down WHICH
            // token/position/layer-count combo failed so the operator
            // can correlate with the per-layer `[MLA]…` lines printed
            // by the lib path. Tensor identity is implicit (the call
            // failed inside the MLA or MoE sub-block of one of the
            // n_layers; lib-side error path emits the specific tensor
            // when verbose logging is on).
            let _ = writeln!(
                arch::serial::Serial,
                "[MoE] gen forward FAILED at gen[{}] token={} position={} n_layers={} (cause: deepseek2 forward returned Err; tensor identity in lib-side trace)",
                generated_count,
                current_token,
                position,
                n_layers,
            );
            gen_abort_reason =
                Some("forward_single_token_deepseek2 returned Err (MLA/MoE sub-block)");
            break;
        }

        let lm_t0 = arch::read_cycles();
        let lm_res = zero_llm_inference::lm_head_argmax_dynamic(
            scratch.hidden,
            output_norm_w,
            output_w,
            output_quant,
            rms_eps,
            embedding_dim,
            vocab_size_padded,
            vocab_size_real,
            scratch.norm_buf,
            logits_buf,
            scratch.scratch,
        );
        gen_lm_cycles = gen_lm_cycles.wrapping_add(arch::read_cycles().wrapping_sub(lm_t0));
        let next_token = match lm_res {
            Ok(t) => t,
            Err(_) => {
                // Why: `lm_head_argmax_dynamic` failed — typically
                // unsupported `output.weight` quant or scratch sizing
                // mismatch. Print enough to disambiguate against the
                // `gen forward FAILED` print above so an operator can
                // tell which post-Phase-1 stage broke first.
                let _ = writeln!(
                    arch::serial::Serial,
                    "[MoE] gen lm_head FAILED at gen[{}] position={} embedding_dim={} vocab_size_padded={} vocab_size_real={} (tensor: output_norm.weight / output.weight)",
                    generated_count,
                    position,
                    embedding_dim,
                    vocab_size_padded,
                    vocab_size_real,
                );
                gen_abort_reason =
                    Some("lm_head_argmax_dynamic returned Err (output_norm / output.weight)");
                break;
            }
        };

        sequence[sequence_len] = next_token;
        sequence_len += 1;
        generated_count += 1;

        let decoded = detokenizer::decode_token(next_token);
        if !decoded.is_empty() {
            let _ = arch::serial::Serial
                .write_str(core::str::from_utf8(decoded).unwrap_or("<non-utf8>"));
        } else if stage11_verbose_logging() {
            let _ = write!(arch::serial::Serial, "[MoE] gen[");
            let _ = write!(arch::serial::Serial, "{}", generated_count - 1);
            let _ = write!(arch::serial::Serial, "] = ");
            let _ = writeln!(arch::serial::Serial, "{}", next_token);
        }

        if eos_matches(next_token) {
            let _ = write!(arch::serial::Serial, "\n[MoE] EOS at gen[");
            let _ = write!(arch::serial::Serial, "{}", generated_count - 1);
            let _ = writeln!(arch::serial::Serial, "], stopping");
            eos_stopped = true;
        }
    }

    let gen_wall_cycles = arch::read_cycles().wrapping_sub(gen_wall_t0);
    let _ = arch::serial::Serial.write_str("\n");
    let _ = write!(arch::serial::Serial, "[MoE] Phase 2 complete: generated=");
    let _ = write!(arch::serial::Serial, "{}", generated_count);
    let _ = write!(arch::serial::Serial, ", eos_stopped=");
    let _ = write!(arch::serial::Serial, "{}", eos_stopped);
    let _ = write!(arch::serial::Serial, ", forward_cycles=");
    let _ = write!(arch::serial::Serial, "{}", gen_forward_cycles);
    let _ = write!(arch::serial::Serial, ", lm_head_cycles=");
    let _ = write!(arch::serial::Serial, "{}", gen_lm_cycles);
    let _ = write!(arch::serial::Serial, ", wall_cycles=");
    let _ = writeln!(arch::serial::Serial, "{}", gen_wall_cycles);

    // ── Final token-id dump for offline replay / verification ────────
    let _ = arch::serial::Serial.write_str("[MoE] sequence: [");
    for (i, t) in sequence[..sequence_len].iter().enumerate() {
        if i > 0 {
            let _ = arch::serial::Serial.write_str(", ");
        }
        let _ = write!(arch::serial::Serial, "{}", *t);
    }
    let _ = arch::serial::Serial.write_str("]\n");

    // ── Record inference timing for bench + control-plane ────────────
    //
    // Mirrors the Qwen3 path (line ~1686). `record_llm_inference` feeds
    // the benchmark screen's tok/s metric; `record_llm_profile` feeds
    // the control-plane `llm` shell command. Both use last-writer-wins
    // semantics (AtomicU64::store), so when multiple LLMs are loaded
    // sequentially (Stage 17 swap) each run overwrites the previous —
    // the bench screen always shows the most-recent model's numbers.
    let generation_compute_cycles = gen_forward_cycles.wrapping_add(gen_lm_cycles);
    // Why: `generated_count == 1` means only the Phase-1 last-token
    // pick produced output and the autoregressive loop broke before any
    // second token — for ROI purposes that is a failure, not "1 tok/s".
    // Route through `record_llm_failure` so the bench screen prints
    // "FAILED — …" with the specific abort reason captured above.
    if generated_count <= 1 {
        let reason = gen_abort_reason
            .unwrap_or("0 tokens generated, forward pass aborted at unknown break (no break-site reason recorded)");
        let _ = writeln!(
            arch::serial::Serial,
            "[MoE] INFERENCE FAILED: generated_count={} reason={}",
            generated_count,
            reason,
        );
        #[cfg(target_arch = "x86_64")]
        crate::bench::record_llm_failure(reason);
    } else {
        let _ = writeln!(arch::serial::Serial, "[DBG] record benchmark");
        #[cfg(target_arch = "x86_64")]
        crate::bench::record_llm_inference(generated_count as u64, generation_compute_cycles);
    }
    #[cfg(target_arch = "x86_64")]
    crate::control_plane::record_llm_profile(crate::control_plane::LlmProfile {
        valid: true,
        run_id: 0,
        prompt_tokens: prefill_count as u64,
        generated_tokens: generated_count as u64,
        prefill_wall_cycles: prefill_wall_cycles,
        prefill_forward_cycles,
        prefill_lm_head_cycles: lm0_cycles,
        prefill_logit_scan_cycles: 0,
        generation_wall_cycles: gen_wall_cycles,
        generation_compute_cycles,
        generation_forward_cycles: gen_forward_cycles,
        generation_lm_head_cycles: gen_lm_cycles,
        generation_sample_cycles: 0,
        generation_logit_scan_cycles: 0,
        generation_render_cycles: 0,
    });
    let _ = writeln!(
        arch::serial::Serial,
        "[MoE] recorded inference: generated={} compute_cycles={} (forward={} lm_head={})",
        generated_count,
        generation_compute_cycles,
        gen_forward_cycles,
        gen_lm_cycles,
    );
}

/// Verbose Stage-11 token-by-token logs are useful for β/sequence hunts,
/// but they go through COM1 and the Cherry framebuffer mirror. Production
/// throughput numbers must measure compute, not KVM text rendering.
#[inline(always)]
fn stage11_verbose_logging() -> bool {
    cfg!(any(feature = "debug-logits", feature = "cherry-smp-debug"))
}

/// Whether Phase-1 prefill should run LM-head for this prompt token.
///
/// Production needs logits at token 0 for the beta anchor only; the
/// first generated token is computed once after the full prompt has
/// populated KV cache. `debug-logits` keeps the older per-token path
/// for Sub-MP-C5 hunts.
#[inline(always)]
fn prefill_lm_head_required(i: usize) -> bool {
    #[cfg(feature = "debug-logits")]
    {
        let _ = i;
        true
    }
    #[cfg(all(not(feature = "debug-logits"), not(feature = "streaming-mode")))]
    {
        i == 0
    }
    #[cfg(all(not(feature = "debug-logits"), feature = "streaming-mode"))]
    {
        let _ = i;
        false
    }
}

/// Top-K diagnostic capture for LM-head logit verification (Sub-MP-C5, V2).
///
/// Per ARCHITECTURE.md V3.1 Pillar 7 (Platform Independence): instrumentation
/// in canonical shared function ensures both platforms execute identical
/// capture path (Lesson 14 Sub-Principle 1, V3.1 Part 4.5-canonized).
///
/// V2 Correction 1: combined Tier 3 (bit-exact
/// via `to_bits()` → u32 → `{:08x}`) + Tier 2 (human-readable via
/// `(logit * 10000.0) as i32`). f32 Display NEVER called — eliminates
/// no_std float-trap (libm linker errors).
///
/// V2 Correction 2 (Option β): explicit `min_in_topk`
/// tracking. In Ring-0 Unikernel we don't trust LLVM register-allocation
/// across ~151k iterations. Local variable guarantees the hot-path
/// threshold check is a single CPU-register comparison.
#[cfg(all(not(feature = "streaming-mode"), feature = "debug-logits"))]
const TOP_K_VERIFICATION: usize = 10;

// Top-K dump is OFF by default. The full 151k-entry scan plus ten
// serial lines per generated token dominate single-core wall-clock
// time on the Cherry Server boot path. The β-anchor PASS/FAIL line
// (`[MP3.0] β-anchor …`) is a separate code path and stays live.
// Build with `--features debug-logits` to restore the dump when
// chasing a Sub-MP-C5 regression.
#[cfg(any(feature = "streaming-mode", not(feature = "debug-logits")))]
fn capture_top_k_logits(_logits: &[f32]) {}

#[cfg(all(not(feature = "streaming-mode"), feature = "debug-logits"))]
fn capture_top_k_logits(logits: &[f32]) {
    let mut top_k: [(u32, f32); TOP_K_VERIFICATION] =
        [(0u32, f32::NEG_INFINITY); TOP_K_VERIFICATION];

    // V2 Correction 2: explicit min-tracking. Initial NEG_INFINITY means
    // the first K iterations always pass threshold and fill the array.
    // Thereafter 99.9% of ~151,936 tokens fail at threshold and skip swap.
    let mut min_in_topk: f32 = f32::NEG_INFINITY;

    for (idx, &logit) in logits.iter().enumerate() {
        // Hot-path: 99.9% exit here. NO array-tail-read, NO swap entry.
        if logit <= min_in_topk {
            continue;
        }

        // Insert at tail + bubble up (≤ K iterations, bounded)
        top_k[TOP_K_VERIFICATION - 1] = (idx as u32, logit);
        let mut i = TOP_K_VERIFICATION - 1;
        while i > 0 && top_k[i].1 > top_k[i - 1].1 {
            top_k.swap(i, i - 1);
            i -= 1;
        }

        // Update min — always tracks smallest (sorted descending, so [K-1])
        min_in_topk = top_k[TOP_K_VERIFICATION - 1].1;
    }

    // V2 Correction 1: combined Tier 3 + Tier 2 output.
    // Tier 3 (logit_bits): bit-exact via to_bits() → {:08x}. PRIMARY.
    // Tier 2 (logit_x10000): human-readable via cast. SECONDARY.
    let _ = writeln!(arch::serial::Serial, "[MP2.5] Top-K logits (K=10):");
    for (rank, &(token_id, logit)) in top_k.iter().enumerate() {
        let bits = logit.to_bits();
        let scaled = (logit * 10000.0) as i32;
        let _ = writeln!(
            arch::serial::Serial,
            "  [{}] token_id={} logit_bits=0x{:08x} logit_x10000={}",
            rank,
            token_id,
            bits,
            scaled
        );
    }
}
